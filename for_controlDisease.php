<h5>
	<span style="font-size:12px;"><span style="color: rgb(128, 0, 0); ">Tobacco Mosaic Virus: <i>Mormor tabaci</i></span></span></h5>
<p>
	<span style="font-size:14px;">The leaves infested with tobacco mosaic virus show dark or light green patches. This is a contagious disease. This disease spreads through contact by labour and implements used for interculture. If the plants are infected in the early stages they show stunted growth reducing the yield and quality considerably. If the disease occurs when the crop is full grown, the tobacco yields are not affected.</span></p>
<p>
	<span style="font-size:14px;">Unclean cultivation, indiscriminately touching diseased and healthy plants, use of tobacco products by workers while working in field, presence of susceptible weeds and crop plants near fields are some of the factors responsible for disease development.</span></p>
<h5>
	<span style="color:#4b0082;"><span style="font-size:12px;">Control Measures</span></span></h5>
<ul>
	<li>
		In areas where TMV is a serious problem resistant varieties like CTRI Sulakshana, VT 1158 are recommended.</li>
	<li>
		Since it is a contagious disease, the following sanitary measures are suggested to prevent the disease spread.</li>
	<li>
		Washing hands in soap water before and after field operations, roguing the diseased plants early in the season and avoiding smoking during field operations.</li>
	<li>
		Prophylactic sprays with virus inhibitors of plant origin like Basella alba and Bougainvillea spectabilis and neem leaf extracts @ 1% dilution on 30th, 40th and 50th day of planting tobacco are useful.</li>
	<li>
		Mosaic affected plants are to be removed within 3 weeks after planting and replaced with healthy seedlings.</li>
</ul>
<h5>
	<span style="font-size:12px;"><span style="color: rgb(128, 0, 0); ">Root-Knot Nematode: <i>Meloidogyne javanica</i></span></span></h5>
<p>
	This is most prevalent in tobacco grown in light soils. Nematode infested plants do not grow vigorously and show stunted growth. The leaves become yellow in colour. In severe cases the plants die.</p>
<h5>
	<span style="color:#4b0082;"><span style="font-size:12px;">Control Measures</span></span></h5>
<p>
	In root-knot affected fields, crops like groundnut, marigold, gingelly, chillies, cotton, redgram are to be grown for two seasons. Deep summer ploughing of the field also helps in reducing the incidence of root-knot.</p>
<h5>
	<span style="font-size:12px;"><span style="color: rgb(128, 0, 0); ">Root Rot: <i>Fusarium Wilt</i></span></span></h5>
<p>
	In FCV tobacco grown in Northern light soils and in burley tobacco grown in agency areas now a days this disease is seen. The leaves on one side of the plants lose vigour and wilt like paralysis on the affected plants with this disease. Gradually complete plant will be affected and finally the plant dies. Initially the root tips show rottening and gradually all the roots rot. The leaves become yellow in colour. The plant will finally die due to lack of supply of water and nutrients.</p>
<h5>
	<span style="color:#4b0082;"><span style="font-size:12px;">Control Measures</span></span></h5>
<p>
	This disease can be controlled by pouring 75 to 100 ml of calyxin 0.05% solution (calyxin 10 ml in 20 litres of water) at the plant bases of infected plants and the surrounding plants. This solution should not be sprayed on the plants.</p>

	<p>
		or</p>
<p> Crop rotation with Maize or Sorghum for a minimum of 2 years. </p>

<p>
		or</p>
<p> Application of Copper hydroxide @0.3% </p>
<p> In KLS region, grow <i>Fusarium </i> resistant variety FCH 222 </p>
<p>
		or</p>
<p>
	By pouring a mixture of mancozeb 20 g and carbendazim (bavistin) 5 g in 10 litres of water at the plant bases of infected plants and surrounding plants @ 75 to 100 ml. This solution can also be sprayed on the plants.</p>
<h5>
	<span style="font-size:12px;"><span style="color: rgb(128, 0, 0); ">Powdery mildew : <i>Erysiphe cichoracearum</i></span></span></h5>
<h5>
	<span style="color:#4b0082;"><span style="font-size:12px;">Control Measures</span></span></h5>
<ul>
	<li>
		Apply sulphur powder (200 mesh) @ 40 kg/ha to soil between plant rows 6-8 weeks after planting. Ash or sand is to be mixed with sulphur for easy application. Care to be taken that sulphur does not fall on tobacco leaves. Recommended for black soils only.</li>
	<li>
		Spraying Karathane 0.2% or  carbendazim 0.05% just before the disease sets in and repeating the same at 10-12 days interval if necessary.</li>
	<li>
		Using resistant varieties like Swarna or Line 2359 developed at Central Tobacco Research Institute, in disease endemic areas.</li>
</ul>


