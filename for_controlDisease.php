<h1 style="text-align: center; "><span style="font-size:16px;"><span style="color: rgb(165, 42, 42); "><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;CONTROL OF DISEASES</strong></span></span></h1>

<h5>
	<span style="color:#0000cd;"><span style="font-size:14px;"><strong><u>A. Diseases in Seed Beds</u></strong></span></span></h5>
<p style="text-align: center; ">
	<span style="font-size:14px;"><strong><span style="color:#800000;">1. Damping off : <i>Pythium Aphanidermatum</i></span></strong></span></p>
<p>
	This is a fungal disease caused by the soil borne pathogen. It is the most common and serious disease in tobacco nurseries causing death of seedlings. Conspicuous symptom of this disease is the sudden collapse of young seedlings in patches leading to uneven stand. Brown watery soft rot of young seedlings, girdling of hypocotyls and finally toppling and death of seedlings leading to wet rot are the characteristic symptoms. The pathogen spreads quickly and affect the seed bed causing enormous loss of seedlings.</p>
<p>
	High humidity, high soil moisture, cloudiness, temperature below 24oC, continuous wet weather, location of nursery in low lying areas are the favourable factors for high incidence of damping off disease.</p>
<h5>
	<span style="color:#800000;"><strong><span style="font-size: 12px; ">Cultural and Preventive Measures:</span></strong></span></h5>
<ul>
	<li>
		Deep ploughing in summer destroys the fungus.</li>
	<li>
		Preparation of raised seed beds 15 cm high with channels around to provide drainage.</li>
	<li>
		Rabbing the seed bed before sowing with slow burning farm waste materials like paddy husk, tobacco stubbles, waste grass etc.</li>
	<li>
		Seed rate @ 3.5 kg/ha is to be used to avoid over crowding of seedlings.</li>
	<li>
		Waterings are regulated to avoid excessive dampness on bed surface.</li>
</ul>
<h5>
	<span style="color:#ff0000;"><span style="font-size:12px;">Chemical control</span></span></h5>
<ul>
	<li>
		Bordeaux mixture @ 0.4% (40 gm of copper sulphate + 40 gm of lime in 10 litres of water) or Fytolan or Blitox @ 0.2% (20 gm in 10 litres of water) is to be applied with rose can 2 weeks after sowing. This has to be repeated once in 4 days under normal weather conditions and once in 2 days under wet and cloudy conditions. In case of heavy rain, the application has to be repeated.</li>
	<li>
		Spraying ridomil MZ 72 W.P. @ 20 g or fenamodone 10%+ mancozeb 50% @ 30 g in 10 litres of water twice at 20 and 30 days after germination. Ridomil should not be sprayed before 20 days after germination or more than two times or more than the recommended dose. (500 g of ridomil is sufficient for one acre of nursery).</li>
</ul>
<p style="text-align: center; ">
	<strong><span style="font-size:14px;"><span style="color:#800000;">2. Black shank and leaf blight : <i>Phytophthora parasitica var. nicotianae</i></span></span></strong></p>
<p>
	This is a soil borne disease. It frequently occurs during mid nursery period causing leaf blight and blackening of roots and stems leading to death of seedlings. Water soaked brown to black lesions appear on the leaf. These patches enlarge and coalesce leading to wet rot of leaf tissue and midribs. Cloudy weather, prolonged dampness and temperature below 22oC are the favourable factors for sudden outbreak of this disease.</p>
<h5>
	<strong><span style="font-size:12px;"><span style="color: rgb(128, 0, 0); ">Cultural and preventive measures</span></span></strong></h5>
<p>
	Black shank can be controlled by following the same schedule suggested for `Damping-off&#39;. Mc.Nair-12 or CM-12(KA) or K-326 which are black shank tolerant varieties are to be used in irrigated northern light soils.</p>
<h5>
	<span style="color:#ff0000;"><strong><span style="font-size: 12px; ">Chemical control</span></strong></span></h5>
<p>
	Spraying Fytolan or Blitox @ 2gin 10 concentration when the seedlings are 50-60 days old controls leaf blight. Ridomil MZ 72 W.P. @ 20 g or Fenamidone + mancozeb 60 % @ 30g in 10 litres of water can also be used to control this disease.</p>
<p style="text-align: center; ">
	<strong><span style="font-size:14px;"><span style="color:#800000;">3. Collar rot : <i>Sclerotium rolfsii</i></span></span></strong></p>
<p>
	Just like damping off, sudden death of seedlings in patches is noticed in seed beds. Blackening of the collar region, wilting and rotting of leaves are the symptoms.</p>
<h5>
	<span style="color:#4b0082;"><strong><span style="font-size:12px;">Control measures</span></strong></span></h5>
<p>
	Spraying 0.4% Bordeaux mixture or Ziram 20 g or Blitox 20 g in 10 litres of water 3 weeks after seed germination. This is to be sprayed twice or thrice at weekly intervals depending upon weather conditions.</p>
<p style="text-align: center; ">
	<strong><span style="font-size:14px;"><span style="color:#800000;">4. Leaf spot diseases</span></span></strong></p>
<p>
	<br />
	<strong><span style="color:#800000;">a. Anthracnose : <i>Colletotrichum tabacum</i></span></strong></p>
<p>
	This disease can be seen in any stage of the nursery. It will cause heavy loss to the nursery when favourable conditions to the fungus prevail. Small, light green to white water soaked lesions develop on young leaves. These lesions enlarge in wet weather to form oily, circular spots of 3 mm diameter. The spots dry up, become papery, thin, grey white surrounded by brown border. Effected leaves become wrinkled and distorted. Elongated, black or brown lesions on the midrib and petiole are seen in severe cases. On the stem, several elongated or oblong sunken brown lesions develop making the seedlings unfit for transplanting.</p>
<p>
	Optimum temperature for the occurrence of this disease is 18oC. High relative humidity, reduced light and overcast weather are favourable for the outbreak of this disease.</p>
<h5>
	<span style="font-size:12px;"><strong><span style="color: rgb(128, 0, 0); ">b. Frog-eye spot : <i>Cercospora nicotianae</i></span></strong></span></h5>
<p>
	Generally this disease is seen 4-5 weeks after germination. Brown, round spots resembling frog eye form appear on the lower leaves of the seedlings. Spots appear first on basal leaves and gradually spread to upper leaves. Spots are small, circular, brown or tan with grey centres. In severe cases coalesce to become bigger spots leading to drying up of leaves.</p>
<p>
	Frequent waterings and wet weather leading to high humidity and temperature around 27oC are favourable for the development of the disease.</p>
<h5>
	<span style="color:#4b0082;"><span style="font-size:12px;">Control Measures</span></span></h5>
<p>
	Spraying with Carbendazim 50 WP 3 gm or pyraclostrobin 5% + metiram 55% WG @ 20 g or Azosystrobin 23% SC @ 0.1% in 10 litres of water at 30 days and 40 days after germination controls this disease. If necessary another spray has to be given after 10 days.</p>
<p style="text-align: center;">
	<span style="color:#800000;"><strong>5. Root-knot nematode: <i>Meloidogyne javanica/ Meloidogyne incognita</i></strong></span></p>
<p>
	The light soil nurseries are prone to this disease. Root knot is caused by nematodes and prevalent in most of the light soil nurseries. Sandy soils and red sandy loams with adequate soil moisture favour the disease development. Several weeds growing in and around nurseries are alternate hosts of root knot nematodes. They are responsible for build-up of the nematodes in the soil.</p>
<p>
	Characteristic symptoms of root knot disease are yellowing of leaves, stunted growth of seedlings, wilting of plants, premature death of seedlings leading to bald patches in seed beds. Affected seedlings when pulled out show several galls on the roots. Galls vary in size, coalesce and produce multiple galls which give the seedlings a sickly appearance. Root knot affected seedlings do not establish well in the field resulting in heavy gaps.</p>
<h5>
	<span style="color:#4b0082;"><span style="font-size:12px;">Control Measures</span></span></h5>
<ul>
	<li>
		Soon after the nursery season, ploughing of the nursery area thoroughly destroys the nematode population by exposing them to the hot sun. The area should be ploughed subsequently, 3 or 4 times during summer months also.</li>
	<li>
		Before raising the nursery, the soil must be tested for nematodes.</li>
	<li>
		Rabbing the seed beds before sowing with paddy husk @ 6 kg/m2 or slow burning farm waste material to destroy nematode larvae and egg masses gives initial protection to seedlings for about a month.</li>
	<li>
		Soil solarisation by covering the nursery soil with 100 gauge white alkathene sheet during summer for about 6-8 weeks controls nematodes effectively.</li>
	<li>
		Nursery should not be grown where brinjal or tomato were grown previously.</li>
	<li>
		Rotation of the nursery area with crops resistant to nematodes like groundnut, redgram, marigold, cotton, gingelly and chillies is recommended. If such rotation crops are grown for more than three years the nematodes can be controlled.</li>
	<li>
		The site of nursery should be changed every year. If one year the nursery is infected with nematodes, nursery should not be grown in that site next year.</li>
	<li>
		Use of tray seedlings fortified with <i> P.lilacinus </i> (50 g) or <i> P. chlamydosporia </i> (50 g).</li>
</ul>
<p style="text-align: center; ">
	<strong><span style="font-size:14px;"><span style="color: rgb(128, 0, 0); ">6. Tobacco Mosaic Virus : <i>Mormor tabaci</i></span></span></strong></p>
<p>
	This is a contagious disease caused by virus. The infested leaves show characteristic mottling with light and dark green patches and seedlings are stunted.</p>
<h5>
	<span style="color:#4b0082;"><strong><span style="font-size:12px;">Control Measures</span></strong></span></h5>
<ul>
	<li>
		Workers should disinfect their hands with soap and running water before weeding or handling seedlings.</li>
	<li>
		Workers should not chew or smoke while at work in nursery.</li>
	<li>
		Diseased seedlings should be removed and destroyed.</li>

<li>
		Phyto sanitary measures are very important for control of the disease.</li>
</ul>
<h5>
	<span style="font-size:14px;"><span style="color:#0000ff;"><strong><u>B. Diseases in field crop</u></strong></span></span></h5>
<p>
	<strong><span style="color:#800000;">Black shank : <i>Phytophthora parasitica f. nicotianae</i></span></strong></p>
<p>
	This is a soil borne disease and it occurs when there is heavy and continuous rainfall, high soil moisture and relative humidity. The blackening of the stem of the diseased plants is noticed upto 30 cm height from the base. At the blackened portion inside the stem, the pith transforms into black round plate like discs. Roots also will be blackened, becomes soft and finally rottening is seen. The leaves of the effected plants becomes yellow, wilt and finally plant dies.</p>
<h5>
	<span style="color:#4b0082;"><span style="font-size:12px;">Control Measures</span></span></h5>
<ul>
	<li>
		In fields known for severe infection with Black shank, Mc.Nair-12 or CM-12 (KA) or Kanchan varieties which are tolerant to black shank are to be used in northern light soils.</li>
	<li>
		Any one of the following fungicides @ 100 ml per plant is to be applied in planting hole and then planting is done.</li>
	<li>
		Bordeaux mixture 0.4% (40 gm of Copper sulphate + 40 gm of lime in 10 litres of water).</li>
	<li>
		Copper oxychloride (Blitox or Fytolan) @ 0.2% (20 gm in 10 litres of water).</li>
</ul>
<h5>
	<strong><span style="font-size:12px;"><span style="color: rgb(128, 0, 0); ">LEAF SPOT DISEASES</span></span></strong></h5>
<h5>
	<span style="color:#800000;"><span style="font-size: 12px; ">a) Frog eye spot : <i>Cercospora nicotianae</i></span></span></h5>
<p>
	The causal agents, symptoms and favourable weather conditions are the same as described under nursery diseases. In addition, this disease also appears as minute dark brown or black spots on stems, midribs, flower stalk and capsules.</p>
<h5>
	<span style="color:#4b0082;"><span style="font-size:12px;">Control Measures</span></span></h5>
<p>
	Spraying Carbendazim 50 WP @ 3 gm or pyraclostrobin 5% + metiram 55% WG @ 20 g  or Azosystrobin 23% SC @ 0.1% in 10 litres of water) at 15 days interval, commencing from 30 days after planting, effectively controls frog eye spot disease.</p>
<h5>
	<span style="font-size:12px;"><span style="color: rgb(128, 0, 0); ">b) Brown spot : <i>Alternaria alternata</i></span></span></h5>
<p>
	Brown spot is a major disease affecting both FCV and non-FCV tobacco&#39;s in India. It is prevalent in most parts of India. This disease generally occurs when there is high relative humidity and atmospheric temperatures around 20-25oC. Especially when dew fall is more, more number of spots will be seen on the leaves. Dark brown or burnt spots separately or coalesced, appear on the diseased leaves. The spots will be like small water soaked spots first, later gradually grow to one cm size in 5-6 days and turns to brown colour. Sometimes yellow rings are seen around the spots just like frog eye spot. When the spots are closely observed, there will be circular rings on the spots. The disease will first appear on the lower leaves and gradually spreads to the upper leaves. Affected leaves dry up and do not cure properly. Dark brown, sunken, elongated spots appear on stems, petioles, seed capsules and stalks also.</p>
<h5>
	<span style="color:#4b0082;"><span style="font-size:12px;">Control Measures</span></span></h5>
<p>
	Mancozeb @ 20 g in 10 litres of water in combination with plantamycin or streptomycin 3 gm  or  propiconazol 10ml / 10 liters of water is to be sprayed on the crop such that all the leaves are covered. For this high volume sprayers like sikar or hi-tech or knapsack sprayers should be used. Depending on the intensity of the disease, two or three sprays should be given at weekly or 10 days interval. Use of excess doses of nitrogen fertilisers should be avoided.</p>
<h5>
	<span style="font-size:12px;"><span style="color: rgb(128, 0, 0); ">Broomrape (Orobanche) : <i>Orobanche cernua</i></span></span></h5>
