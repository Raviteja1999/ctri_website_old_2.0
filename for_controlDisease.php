<h5>
	<span style="color:#4b0082;"><span style="font-size: 12px;">&nbsp;</span><span style="font-size:12px;">Control Measures</span></span></h5>
<ul>
	<li>
		Deep ploughing twice or thrice in summer buries Orobanche seed to deeper depth and there by helps in reducing the emergence of Orobanche.</li>
	<li>
		In Orobanche sick fields, growing tobacco for one or two seasons is to be skipped off.</li>
	<li>
		Avoiding growing of brinjal, tomato and bhendi crops in the sick fields.</li>
	<li>
		Periodical removal of Orobanche shoots before flowering and setting of seeds reduces the menace. The Orobanche shoots should be destroyed by burning.</li>
	<li>
		Growing trap crops such as jowar, gingelly, blackgram and greengram in kharif facilitates orobanche germination but will not allow it to grow. This reduces the Orobanche seed load in the soil.</li>
	<li>
		CTRI developed a spear with a 2 m stick at the end of which 18 cm length, 8 cm breadth and 0.5 cm thick sharp iron blade is arranged. Cutting the Orobanche shoots within 3-4 days after emergence out of the soil, before flowering of Orobanche either upto the soil level or 2-3 cm below the soil level with spear effectively controls orobanche.</li>
</ul>
