<h1 style="text-align: center; "><span style="font-size:16px;"><span style="color: rgb(165, 42, 42); "><strong>QUALITY PARAMETERS</strong></span></span></h1>

<p style="text-align: justify;">
	<span style="font-size:14px;">The concept of quality in flue-cured tobacco has attained a new dimension in the present days because of higher mobility in international market, ever growing sophistication among smokers taste and increasing automation in cigarette industry. While overall monetary return is a good enough criterion of quality to a farmer, visible quality like colour, is very important for marketing purposes; but to an industrialist, those manufacturing qualities which maximise profit are most desirable features provided tobacco possesses satisfactory smoking quality which inturn depends upon chemical composition of leaf material. Flue-cured tobacco quality thus is made up of many complex components, viz. physical, chemical, organoleptic and also economic attributes. Smoking quality plus manufacturing capacity equals the suitability of tobacco. So the quality characters of FCV tobacco are devided into visual characters, manufacturing characters and chemical characters.</span></p>
<p style="text-align: justify;">
	<span style="font-size:14px;">The visual characters are colour, body, texture, maturity/ ripeness, graininess, hygroscopicity, shatterability, blemish, elasticity, fluffiness, aroma, leaf size, vein colour etc. These characters are subjective based on which the tobacco leaf is graded and purchased by the trader. Objective quality criteria have been developed for the manufacuring characters and chemical characters. The manufacturing characters are filling value, equilibrium moisture content, pore volume, elasticity, shatterability, combustibility, lamina-midrib ratio (Strip yield), No. of leaves/kg, lamina weight/unit area etc. Though tobacco leaf contains hundreds of chemical constituents, only few of them will have dominating influence on quality. Nitrogenous and carbohydrate fractions are the two groups of chemical constituents having outstanding effect on smoking quality. Chloride in leaf is also very important as it influences the combustibility and keeping quality of leaf.</span></p>
<h2>
	<span style="color:#a52a2a;"><b>Physical Properties</b></span></h2>
<p>
	<span style="font-size:14px;">Tobacco buyers evaluate tobacco by its visual characteristics. Such a system of subjective quality evaluation varies with personal fancies and hence cannot be considered as precise. The necessity of objective laboratory tests to evaluate physical qualities has become essential.</span></p>
<h2>
	<span style="color:#a52a2a;">Filling Value</span></h2>
<p style="text-align: justify;">
	<span style="font-size:14px;">Filling value is the volume occupied by unit weight of cut tobacco at predetermined moisture level under a specific stress. Filling value may be taken to be a measure of the relative number of cigarettes of a given firmness that could be manufactured from a unit weight of tobacco. Good filling value enables the manufacturer to ensure that cigarettes are made as well filled rigid rods. Poor filling results in poorly made soft cigarettes from the ends of which tobacco shreds fall out easily or else cigarette weight has to be increased in order to restore cigarette firmness. Heavy filling on the other hand can result in too much draw resistance and in sharply altered burn characteristics. Dimensionally, filling value is inversely related to lamina weight. Hence, very thin bodied leaf resulting in `chaffiness&#39; also shows a high filling value and as such it is of undesirable quality. There is an optimum filling value for high quality leaf.</span></p>
<h2>
	<span style="color:#a52a2a;">Shatterability</span></h2>
<p>
	<span style="font-size:14px;">Another important economic factor in tobacco quality is its resistance to breakage during handling. Tobacco is a fragile material that tends to shatter to a great or less degree with each handling. Breakage becomes accentuated under the stress of mechanical processes in the factory. Tobaccos do differ in their relative brittleness due to various factors. Strength in tobacco leaf is dependent on calcium pectate, the cementing material in the cell wall. Although moisture content influences this property to a great extent, other unknown factors may also play the role in controlling brittleness of leaf.</span></p>
<h2>
	<span style="color:#a52a2a;">Strip Yield</span></h2>
<p style="text-align: justify;">
	<span style="font-size:14px;">Strip yield in flue-cured tobacco is important to manufacturers since it is the lamina portion of leaf that is normally used in cigarette making. Because of the low utility of midrib, a large bulk of exportable flue-cured leaf is despatched only in the form of strips. This makes strip yield an important criterion in developing varieties as the higher the strip yield, the greater is the economic return. The strip constitutes, on an average, about 75% of leaf by weight, usually ranging from 70-80%. Within this range, the higher the strip yield, the better the usability of tobacco.</span></p>
<h2>
	<span style="color:#a52a2a;">Elasticity</span></h2>
<p>
	<span style="font-size:14px;">Elasticity is considered to be a major quality factor in tobacco. Elasticity is the ability of the leaf, when moist, to undergo stretching without breaking. Such tobaccos after being compressed, as occur during cutting in the manufacture of cigarettes, will spring back immediately. Springiness in flue-cured tobacco is thus due to its elasticity. Elasticity is dependent upon water soluble constituents, and stands in direct correlation with the moisture content in tobacco.</span></p>
<h2>
	<span style="color:#a52a2a;">Texture or Porosity</span></h2>
<p style="text-align: justify;">
	<span style="font-size:14px;">Leaf structure or texture is an important physical property of flue-cured tobacco. Texture and grain are synonyms for cigarette tobacco. Graininess in flue cured tobacco is a measure of porosity of leaf which regulates its capacity to absorb and retain additives in the intercellular air chamber. Leaf structure is also defined as degree of cell development of leaf as indicated by its porosity.</span></p>
<h2>
	<span style="color:#a52a2a;">Hygroscopicity</span></h2>
<p style="text-align: justify;">
	<span style="font-size:14px;">Hygroscopic properties of cured leaf as judged by equilibrium moisture content is an important technological criterion for judging quality. It is the moisture absorbing capacity of leaf which depends on the relative humidity of the surrounding environment. Cured leaf low in hygroscopicity is very difficult to get to `order&#39; or `condition&#39; with the result the leaf handling is impeded. High hygroscopicity on the other hand, entails operational difficulty in the cutting and filling machine. Storage of heavily hygroscopic leaf is also a problem. Cigarettes having very low moisture content give harsh taste and smoke; high moisture content on the other hand impairs combustibility. Therefore, it is apparent that cured leaf of acceptable quality should have its equilibrium moisture content within the optimum range i.e. 12 to 14% at 60% relative humidity. Lipophilic colloidal constituents of leaf materials and tissue density (porosity) have direct influence on the water absorption and retention capacities. Reducing sugars are known to increase leaf moisture content. The hygroscopicity of leaf is increased with chloride content of the leaf. The water holding capacity of cured leaf increased with potassium content. The effect of potassium may possibly be due to increase in the level of soluble carbohydrate synthesis.</span></p>
<h2>
	<span style="color:#a52a2a;">Combustibility</span></h2>
<p style="text-align: justify;">
	<span style="font-size:14px;">Combustibility or burning quality of tobacco involves several criteria like fire holding capacity, rate of burn, evenness or completeness of burn and character of residual ash. Leaf burn is very commonly used to determine the burning quality of cured leaf. This test is done by touching a piece of manually stretched pre-conditioned leaf to glowing nichrome wire and noting number of seconds the glow continues. Usually 3-5 seconds burn is considered to be satisfactory. Rates of burn vary with different types of tobacco. The cigar filler because of its thickness is slow burning. In the cigarette a free, more rapid combustion takes place due to the small diameter of the filler and specially fine shredding of leaf which increases surface exposure. Factors affecting combustibility are both physical and chemical in nature. Among the physical characteristics, micro structure of leaf is more important. Thick heavy leaf with fine texture due to a close cell packing would have a poor burn since there would be less air space between the cells and consequently poor aeration during burning. Good burning leaf has a loose open structure having high porosity which was likely to promote burn by better aeration. Good burn is always exhibited by leaf containing high potassium and low chloride. Calcium and magnesium control completion of burning process and production of white ash.</span></p>
<h2>
	<span style="color:#a52a2a;"><b>Total Nitrogen Content</b></span></h2>
<p style="text-align: justify;">
	<span style="font-size:14px;">It is generally considered that flavour and taste of smoke is correlated with the nitrogenous constituents. Flue-cured tobacco containing 1.6 to 2.3% total nitrogen gives the most satisfying smoke. Higher nitrogen content of tobacco would result in, apart from curing difficulty, deep brown coloured trashy leaf which shatters readily and it has flat-insipid tasting smoke. Generally high level of nitrogen is associated with high level of nicotine. Lower nitrogen content would result in `washed out&#39;, pale coloured leaf, lacking in rich colour characteristic of good tobacco.</span></p>
<h2>
	<span style="color:#a52a2a;">Nicotine Content</span></h2>
<p>
	<span style="font-size:14px;">Nicotine content of tobacco by virtue of its stimulatory effect on the smoker is next important constituent. It is considered that a nicotine level of 1.75 to 2.0% in FCV tobacco is most satisfactory. The nornicotine in acceptable tobacco should not exceed 5% of total alkaloids. High proportion of nornicotine in cigarette leaf leads to abnormal and objectionable smoke flavour due to pyrolysis of nornicotine into myosmine.</span></p>
<h2>
	<span style="color:#a52a2a;">Nitrogen/Nicotine Ratio</span></h2>
<p style="text-align: justify;">
	<span style="font-size:14px;">The ratio of nitrogen to nicotine is assumed to give some chemical balance within the leaf. The higher the ratio the less desirable the tobacco because it tends to be light bodied. A ratio in the neighbourhood of 1.35 gives way to paleness of colour, slickness of texture and a general lack of desirable physical characters and deficiency in aroma. In fact, a value exceeding 1.0 has been ascribed as unbalanced. Too low a value (below 0.5), on the otherhand, frequently may be considered undesirable because the tobacco is heavy bodied and associated with high nicotine content and low level of reducing sugars. A range of 0.6 - 0.7 ratio has been adjudged as most desirable in medium to light bodied matured tobacco.</span></p>
<h2 style="text-align: center;">
	<span style="color:#a52a2a;"><span style="font-size: 14px;">Reducing Sugars</span></span></h2>
<p style="text-align: justify;">
	<span style="font-size:14px;">Higher content of reducing sugars in flue cured tobacco is undesirable as it imparts to the smoke an acidic character. Lower content imparts alkalinity to smoke due to high nitrogenous constituents. During smoking, sugars are burnt out as CO2 and water, thus helping to neutralise free base and increase moisture content in smoke and so act as an emollient, if present in excessive quality.</span></p>
<h2>
	<span style="color:#a52a2a;">Reducing Sugars/Nicotine Ratio</span></h2>
<p>
	<span style="font-size:14px;">The ratio of sugar to nicotine would give a balance of opposing effects and thus serve as a good smoking quality indicator. A high ratio may tend to indicate mildness and smoothness while a very low ratio may be indicative of harsh irritating smoke. If the ratio is too high, it may indicate that the tobacco is too mild to be acceptable to smoker. If cured leaf contains both low level of nicotine and sugars as generally is the case with Indian flue-cured tobaccos, the ratio appear to be comfortable. High sugar content consistent with nicotine level is the most desirable feature for smoking quality in flue-cured tobacco. The desirable ratio is 7-13.</span></p>
<h2>
	<span style="color:#a52a2a;">Carbohydrate/Protein Ratio</span></h2>
<p>
	<span style="font-size:14px;">The ratio of carbohydrate to proteins is known as Shmuk number and the ratio of carbohydrate to total nitrogen is known as Kovalenko coefficient. These ratios will show similar effect of reducing sugar to nicotine.</span></p>
<h2>
	<span style="color:#a52a2a;">Chlorides</span></h2>
<p style="text-align: justify;">
	<span style="font-size:14px;">High level of chlorine in leaf inhibits leaf burn or combustibility. The chlorine content of leaf must be preferably less than 1.5% but should never exceed 2%. Chlorine content is positively correlated with deterioration of colour. High chloride content in leaf leads to dull muddy orange colour with sour or linoleum smell. Further, such leaf due to its moisture holding capacity bruises easily and tends to develop `off-colour&#39;. These characteristics render this type of leaf to be of low value for cigarette manufacture. Chlorine acts as a negative combustion catalyst in tobacco.</span></p>
<h2>
	<span style="color:#a52a2a;">Potassium</span></h2>
<p>
	<span style="font-size:14px;">Potassium content in the cured leaf was found to improve the burning quality of tobacco. An adequate level of potassium in cured leaf tends to off-set the deleterious effects of high chlorine on burning quality. Potassium acts as a mineral catalyst and oxygen carrier in promoting burn of tobacco leaf. Cured leaf lacking in potassium content would result in poor coloured trashy leaf and looses its lustre which may not have any commercial value.</span></p>
