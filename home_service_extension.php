<h1 style="text-align: center; ">
	<span style="font-size:16px;"><span style="color: rgb(165, 42, 42); "><strong>Agricultural Extension</strong></span></span></h1>
	

<br>
<p>The dissemination of developed technologies enabled the farming community to adopt the technological interventions as per the recommendation. Most of the tobacco farmers in general  and FCV tobacco farmers in particular have been adopting the superior varieties, improved agro practices and thus enhanced the production efficiency & product quality. </p>

<p>
<b>Technology Evaluation and Demonstration</b>
<p>
<b>On farm trial:</b> Evaluating proven research results under farmers conditions for knowing its suitability.<br>
<p>
<b>On farm testing:</b> Confirmation of already proven research results under farmer conditions for wider adoptability.<br>
<p>
<b>Front line demonstration:</b>  Conducting demonstrations under the close supervision of scientists because the technologies are demonstrated for the first time by the scientists themselves for knowing field level constraints and for getting direct feedback from the farmers.<br>
<p>

<b>Field demonstration:</b> Demonstrations to educate the farmers about proven technologies. <q>seeing is believing</q> is the basic philosophy.

<br>
<p>

<b>Training Programmes:</b>
<p> 
Need based training programmes are conducted for tobacco farmers, field staff of Tobacco Board & other stakeholders of tobacco industry in order to bridge the technology adoption gap in the real farm situation and to ensure economic sustainability. </p><p>
During past five years, a total of 195 training programmes were conducted in 305 villages on innovative and viable technologies addressing nursery, crop production, crop protection, post-harvest and product management issues covering about 7800 farmers. In addition, six training programmes were conducted for extension functionaries of Tobacco Board and Tobacco industry and upgraded their knowledge in diversified areas of tobacco production </p>
<br>
<p>
<b>Analysis of Success Stories</b>
 
<p>An in depth analysis of success stories of recognized farmers highlighted the technologies which were found to be having high technical efficiency in determining the yields and quality at real farm situation. Further, the crops that are having substantial acreage under their cultivation in FCV tobacco growing areas are considered as most likely next best crops for growing in FCV tobacco areas.</p>
<br>
<p>
<b>Innovative Methodologies Adopted by ICAR-CTRI</b>

<p>To attain much promise in sharpening of knowledge and skills of tobacco farmers, ICAR-Central tobacco research institute, Rajahmundry, A.P. has adopted the innovative methodologies like Model Project Area Approach, Field Friends, crop phase wise training programmes, mobile based advisory services and implementing them in convergence with Tobacco Board and other tobacco stakeholders. The methodologies are intended to promote the adoption of innovative technologies for achieving increased yields, quality and farm returns.</p>
<p>
<b>Convergence approach for training and creating awareness on new technologies:</b></p>

<p>Many extension service providers in the field are providing different kinds of services and support to farmers. There is duplication of efforts and attending to extension work without convergence. A coordinated effort was taken up by representatives from ICAR&#45;CTRI scientists, Tobacco Board officials, Trade members and progressive farmers in addressing the issues in a participatory mode: ICAR&#45;CTRI : FARMERS: TOBACCO BOARD: TRADE. The approach is adopted every season in all the regions of tobacco cultivation falling under different auction platforms of Tobacco Board. Village level awareness and training campaigns were conducted during crop season.</p>

<p><b>Model Project Area: </b></p>
<p>
Low productive areas were selected for implementation of model project area approach under different FCV tobacco micro-zones. Tobacco Board conducted demonstrations on latest technologies on FCV tobacco cultivation in collaboration with ICAR-CTRI in these villages. This provided an opportunity to implement recommended technological interventions and to evaluate technology performance. Model project area served as nerve centre in assessing and demonstrates technology potential and also helped in up scaling the technology demonstrated.
</p>
<p><b>Field Friends Programmes</b></p>
<p>The potential extension providers in tobacco are identified and made partners in field friends programmes. Together visiting tobacco fields, identify the major problem if any and advocating viable remedy if farmer is readily present. If farmer is not available, display the remedy on suitable paper and keeping it as a flag in the field.</p>
 
<p><b>Mobile based advisory services:</b></p>
<p>Extension in FCV tobacco is now becoming more diversified, technology intensive, knowledge oriented and more demand driven. Mobile phone penetration in rural areas expanded rapidly. Hence, using mobiles to communicate tobacco technology directly to farmers at a faster rate. Effective use of mobile Aps helps in bridging knowledge deficiency among farmers. Developing mobile Aps through which the necessary information gets transmitted at one go to all the registered farmers of the tobacco area.</p>

<p><b>Progressive farmer circles</b></p>

<p>Farmers are the best educators of other farmers. So farmer to farmer extension visits can greatly help in information exchange and dissemination. The approach facilitates for effective action, observation and feedback. Identified achiever farmers in different agro-climatic micro zones. Two-three achiever farmers share their success/experience with other farmers during group meetings and field visits. Scientists will act as facilitators. The topics covered can vary considerably i.e IPM, INM, ICM, organic cultivation, PHPM etc.</p>

<p><b>Documenting farmer&#39;s knowledge</b></p>

<p>Many farmers have a scientific approach and practical knowledge but hardly document their experience. Farmers creativity and capacity for problem solving are being recognized and documented as success stories.</p>
<p>
To make technology dissemination more demand driven and efficient in reaching small and marginal farmers, ICAR- CTRI is involving the farmers and other stakeholders in various activities and extending advisory services with increased relevance. The dissemination of developed technologies enabled the farming community to adopt the technological interventions as per the recommendation. Most of the tobacco farmers in general  and FCV tobacco farmers in particular have been adopting the superior varieties, improved agro practices and thus enhanced the production efficiency & product quality. Adoption of high yielding varieties, viable production and protection technologies has led to quantum jump in productivity levels . Further improvement in physical and chemical leaf quality attributes of Indian tobacco promoted the acceptability and saleability of Indian tobacco in the global market.  All the extension activities of ICAR-CTRI and its regional stations have received very good response and participation from the farming community in different tobacco growing regions </p>

