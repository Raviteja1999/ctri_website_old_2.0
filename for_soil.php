<h1 style="text-align: center; ">
	<span style="font-size:16px;"><span style="color: rgb(165, 42, 42); ">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>Soils &amp; Climate</strong></span></span></h1>


	
<p>
	Tobacco is one of the important commercial crops cultivated in India. India is the third largest producer of tobacco in the world (721 M.kg) after China (2997 M.kg) and Brazil (862 M.kg) contributing the tune of Rs 6,058 crores as foreign exchange and 23,318 crores as excise revenue to the national exchequer. Tobacco is a polymorphic crop, various tobacco types viz., Flue-cured Virginia (FCV), Burley, <em>Bidi</em>, Chewing, <em>Natu</em>, Oriental, HDBRG, Lanka, Hookah, <em>Motihari</em> and <em>Jati</em> tobaccos are cultivated in different agro-ecological situations in the country.  Unlike many other crops, tobacco is very sensitive to edaphic, climatic/ weather and management variations.</p>
<br>
<h5 style="text-align: center; ">
	<span style="color:#b22222;"><span style="font-size: 16px; ">CLIMATE</span></span></h5>
<p>
	Tobacco is tropical in origin, but it is grown successfully under tropical, sub-tropical and temperate climates. Normally it requires about 100 to 120 days  frost-free climate with an average temperature of 80&deg;F to mature and a liberal and well distributed rainfall of 88 to 125 mm per month are ideal requirements for tobacco crop. Relative humidity may vary from 70-80% in the morning to 50-60%  in the mid day. In India, tobacco is grown under a very wide range of climatic conditions from the coast-line to an altitude of 3,000 feet. In the South, the crop is raised in winter from October to March when the temperatures are moderate. Important climatic factors which influence the growth, flowering and metabolism of tobacco plant are Rainfall, Temperature, Relative humidity, Wind and Sunlight</p>
<p>
	 Ideal distribution of rainfall for best yield and quality include cloudy weather with drizzling (soon after transplanting), light rain fall in Early growth stage, bright sunshine with occasional moderate rain in Grand Growth period and No rains at all in ripening (maturity) of leaves because the rain will wash away the gums from the leaf surface resulting in poor curing. For maintaining the turgidity and expansion of leaf and to meet the transpiration losses of moisture from its enormous leaf area, tobacco plant needs considerable amount of water. On the other hand, tobacco plants are very sensitive to flooded or waterlogged condition of soil because of oxygen deprivation in soil essential for the development of fibrous and vigorous root system. Hence heavy clayey soils are ruled out for tobacco cultivation in monsoon due to the inundation problem. Light soils can be used for taking up a rainfed tobacco crop provided the rains are well distributed during the growing  season as in the case of Karnataka light soils.Tobacco crop can also be grown on light sandy soils under irrigated condition during dry months in winter as in the Northern light soils of Andhra Pradesh. In India, rains are heavy and uneven for a limited period during the South-West monsoons.  Atmospheric temperatures are higher than the optimum level  except in Hunsur and hence  crop is grown in winter period of October to March when the temperatures are suitable to FCV tobacco in Andhra Pradesh. </p>
<p>
	<strong><span style="color:#b22222;">FCV TOBACCO</span></strong></p>
<p><strong><span style="color:blue;"alignt="left">&nbsp;&nbsp;Andhra Pradesh</span></strong></p>
<p>
	The annual rainfall through South-West monsoon is around 800- 1000 mm starting from June to October. The crop is exclusively grown on conserved soil moisture during rabi season extending from October-November to February-March in black soils and under irrigated conditions in Northern Light soils. In the Prakasam and Nellore district light soils the crop is grown with the help of unprecedented rain occasionally received through the North- East monsoon cyclonic storms which will help in enhancing yields. The crop requires good sun light, low evaporation, high relative humidity, sustained soil moisture in black soils and free from wet footing and cyclonic storms.</p>

<strong><span style="color:blue;"alignt="left">&nbsp;&nbsp;Karnataka</span></strong></p>
<p>
	Mysore district is under the influence of tropical monsoonic climate with an average annual rainfall varying from 650 mm to 1000 mm of which over 80% is received during the south-west monsoon period from May to October which coincides with the FCV tobacco crop season. February to April are the dry summer months. The mean minimum temperature in the cold winter month of January is around 15&deg;C while the mean minimum temperature in the hot summer month of April is 34&deg;C. Thus, the transitional zone of Mysore tract is endowed with ideal climatic conditions like lower diurnal temperatures, higher relative humidity and monsoonic rains during the crop growth period from May to September which are highly conducive for production of high quality flue cured tobacco.</p>


<p>
	<strong><span style="color:#b22222;"><em>Moitihari and Jati </em>Tobacco</span></strong></p>
<p>
	<em>Moitihari and Jati </em>tobaccos are grown in North Bengal and the climate is characterised by humid and damp weather throughout the year. The day temperature varies from as low as 5&deg;C during December-January (winter) to as high as 35&deg;C during April-May (summer). The rains of North Bengal are long and persistent and the average annual precipitation exceeds 3000 mm, major part of which is received during the month of June, July and August. Relative humidity of the atmosphere is about 85% during April and May facilitating easy handling and conditioning of cured leaf.</p>

<p>
	<strong><span style="color:#b22222;">Chewing Tobacco</span></strong></p>
<p>
	<b>Bihar :</b>   Climate that prevails in North Bihar is quite suitable for growth, development and quality leaf production of chewing and hookah types of tobacco as well as some other types. The warm and humid weather of 2nd fortnight of October is considered congenial for establishment of transplants with desirable initial growth. Low temperature, bright sunshine and low humidity during early growth stage of crop plants help, in build-up of thick and heavy bodied leaf, thus producing higher leaf yield with desirable physical and chemical quality parameters.</p>
<p>
	<b>Tamil Nadu:</b> Chewing tobacco is grown under irrigated condition during rabi season in Coimbatore, Salem, Thanjavur, Madurai, Tirunelveli and South Arcot districts. The average rain fall of the region was 600-800 mm.</p>

<p>
	<strong><span style="color:#b22222;"><em>Bidi </em>Tobacco</span></strong></p>

<p>
	The <em>bidi</em> tobacco in Charotar area of middle Gujarat is grown with few irrigations and the average annual rainfall 850-900 mm. In Gujarat the crop comes to maturity during winter due to which the leaf is thick and becomes rugged. This is desirable feature for bidi tobacco. In Nipani area of Karnataka it is mostly grown under rainfed conditions. A less irrigated bidi tobacco is more prized than a more irrigated crop. In the Kurnool district of Andhra Pradesh, bidi tobacco is grown as a rainfed crop with an annual average rainfall is 670.5 mm, 67% rainfall received during South West monsoons and 23% received during North West monsoons.</p>

<p>
	<strong><span style="color:#b22222;"><em>Rustica</em>Tobacco</span></strong></p>


<p>
	Rustica tobacco is grown mostly in Uttar Pradesh, Gujarat and West Bengal (Motihari Tobacco) Climate in Uttar Pradesh is  semi-arid with an average rainfall about 900 mm. More than 70% of total rainfall is received in the months of June, July and August, while winter showers are meagre. Low temperature, low humidity, bright sunshine during the crop growth are essentially required for production of quality tobacco. Nurseries of the winter crop are sown in August- September and those for summer crop in February- March. The two crops are raised during October-April and April-June, respectively.  In Gujarat rustica tobacco is grown in Sabarkanta, Banaskanta , Nadiad, Kambat, Mehsana ans Anand districts with an average annual rainfall of 800 mm.   </p>


<p>
	<strong><span style="color:#b22222;">Lanka Tobacco: </span></strong></p>
<p>
	The Lanka crop is mainly grown in Andhra Pradesh which require  80 to 90% relative humidity during plant growth period from mid October to November ending with a mean day temperature of about 26.7&deg;C and the crop needs 30 to 32&deg;C so that the leaves on the plant will come to maturity with raising temperatures.</p>

<h5 style="text-align: center; ">
	<span style="color:#b22222;"><span style="font-size: 16px; ">SOILS</span></span></h5>
<p>
	Soils play a significant role in deciding the quality and commercial value of tobacco. The type, grade and quality of tobacco produced are, to a large extent influenced by the soil characteristics particularly the texture and colour of surface and subsoil.</p>

<p>
	<strong><span style="color:#b22222;">FCV TOBACCO</span></strong></p>
<p>
	Among the different types, FCV tobacco is an exportable type which  occupies 32% of the total tobacco cultivation in India. FCV tobacco in India is cultivated mostly in Andhra Pradesh and Karnataka. Soil and climate are the two very important factors which determines the  suitability  of a region  for cultivation of FCV tobacco crop </p>
<p>
	 Flue-cured Virginia tobacco is grown on different types of soils ranging from sands to sandy loams of East Godavari, West Godavari and Khammam districts, the red loams of Prakasam and Nellore districts and heavy black cotton soils of Guntur, Krishna, Prakasam and East Godavari districts. These soils differ widely in texture and in soil fertility. In an open, light textured soil, tobacco roots have been found at a depth of 120 cm utilising moisture and plant nutrients from such deep layers of soil. Three major soil conditions that contribute to maximum leaf expansion are adequate supply of oxygen, water and plant nutrients. Light soils produce thin and large bodied leaf, light in weight and color, mild in strength and weak in aroma whereas leaf produced in heavy soils is usually thick and heavy, dark colored, strong and aromatic. Natural resources viz., soil and water are to be judiciously utilized for sustainable tobacco production. Soil constraints/limitations are to be studied comprehensively to find out suitable interventions for their successful management.</p>

<p><strong><span style="color:blue;"alignt="left">&nbsp;&nbsp;Ideal soil characteristics</span></strong></p>
<li>	Sandy surface soil upto 15 to 25 cm depth. </li>
<li>	Sandy clay sub-soil extending upto a depth of 150 cm. </li>
<li>	Acidic soil reaction ( pH 5.5 to 6.5) </li>
<li>	Low reserve of essential plant nutrients.</li>
<li>	Low organic matter.</li>
<li>	Very low chloride content (less than 100 ppm). </li>
<li>	Soil should be free-draining and well-aerated throughout the season.</li>
<li>	The fertility status of the soils should not be high.  In fact, nitrogen starvation condition should prevail at the time of leaf maturation.</li>

<p>
	<strong><span style="color:#b22222;">Black Soils (Vertisols) </span></strong></p>
<p>
	Tobacco is raised as a rainfed rabi crop with the conserved soil moisture of south west monsoon rains. Soils are alkaline, clayey in texture (40-60% clay) with high water holding capacity (70%), high fertility and impeded drainage, rich in Ca and K. (4.5 to 5% Ca) and hydraulic conductivity is very poor indicating easy water logging. These soils have high cation exchange capacity  and soil adsorption sites are fully saturated with exchangeable bases mainly calcium, magnesium, potassium and sodium. Buffering capacity of these soils is high. Swelling and sticky properties when wet and the shrinkage and hard cloddy nature of the soil when dry pose a great problem in the management. In these soils the infiltration rate is as low as 0.2cm/hr and hydraulic conductivity is as low as 0.15cm/hr, hence there is a possibility of submergence during the rainy season. The slow permeability is associated with black clay soils, creating oxygen stress in the root zone due to stagnation of water. Addition of organic manures, providing proper drainage i.e creation of field channels along the plots and balanced fertilization/integrated nutrient management practices are some of the management interventions required for cultivation of FCV tobacco

</p>

<p>
	<strong><span style="color:#b22222;">Light Soils</span></strong></p>
<p>

	<b>Northern Light-Soils</b> : These soils are derived from sand stones and are sandy to sandy loamy in texture moderately acidic in soil reaction, well drained, low in CEC and water holding capacity, loose and highly porous, percent calcium saturation in these soils is 20-25%. As they are very poor in fertility and require adequate supplies of fertilizer elements viz., nitrogen, phosphorus, potassium, calcium and magnesium which help in successful production of quality tobacco. In Andhra Pradesh these light soils are distributed in East Godavari, West Godavari and Khammam districts popularly called the Northern light soils (NLS). The crop in Northern light soil regions is raised under assured irrigation sources and crop yields are high.   Fertilizer and water use efficiency of these soils is very low and nutrient losses through leaching are high. Application of organic manures, pond sediments or mixing of clay decreases the bigger pores and increases the smaller pores, thus reduces the hydraulic conductivity and increases the water retention. 

</p>


<p>
	<b>Southern Light Soils</b> :In Prakasam and Nellore districts, FCV tobacco is grown on medium textured red soils and are popularly called as Southern light soils (SLS). These soils contain moderate quantities of clay, soils are neutral to slightly alkaline, low in organic matter, low in P and medium to high in available potassium contents. They are moderately well drained with moderate to slow permeability. Cation exchange capacity and percentage base saturation is also medium. FCV tobacco is grown on conserved soil moisture mainly from the rainfall of the North-East Monsoons. Soil crusting is one of the  problems observed in some of the red soils in Prakasam and Nellore districts. Crusting tendency can be reduced by addition of farm yard manure, soil surface covers such as mulches, plant residue, close growing of the crops, grasses and legumes prevent the crust formation.  Keeping the soil moist by irrigating the crops also helps in reducing the crust formation. Soil mulching, use of tine cultivator, green manuring and green leaf manuring, Soil test based balanced fertilizer use, water harvesting and recycling for life saving irrigation, use of ground water by mixing it with harvested rain water are some of the interventions suggested for cultivation of FCV tobacco
</p>

<p>
	<b>Karnataka Light Soils</b> : Karnataka light soils range from sandy loams to sandy clay loams well drained characterised by the dominance of 1:1 clay minerals unlike Andhra Pradesh black soils which are predominantly 2:1 clay minerals. Soils are slightly acidic, low in inherent fertility with fairly good water-holding capacity. FCV tobacco is grown as a monsoon crop during Kharif season.
</p>


<p>
	<b>Irrigation Water Quality</b>  : Chlorides in the irrigation water should be < 50 ppm. Irrigation with high chlorides lead to production of saline leaf. Chloride content in cured leaf should be < 1.5%, as high chlorides reduces the burning quality of the tobacco leaf. 
</p>
<p>
	<b>HDBRG Tobacco</b>: HDBRG tobacco is grown in Central black soils of Andhra Pradesh where the FCV tobacco is also grown.
</p>
<p>
	<strong><span style="color:#b22222;">Burley Tobacco </span></strong></p>
<p>
The burley tobacco is grown during monsoon season in Agency area of East and West Godavari districts of Andhra Pradesh and the soils are generally sandy loams on the surface and loams at the sub-soil level and the pH is neutral to acidic (5.5 to 6.5 pH) in reaction. The soils are very low in soluble salts, chlorides and light in texture. The organic matter content, available nitrogen and available phosphorus status is low. In the recent past burley tobacco cultivation is extended to Vinukonda area of Guntur district in areas of 15000 ha under rainded conditions. Soils are red gravelly poor in soil fertility.</p>

<p>
	<strong><span style="color:#b22222;">Natu Tobacco </span></strong></p>
<p>

The crop is raised on medium to heavy black soils with a pH range of 7.0 to 8.5. These soils are poor in available nitrogen, organic carbon, medium to high in phosphorus and high in available potash. It is also grown on sandy to sandy loams with low organic carbon, low in available N, P and K as an irrigated crop.</p>
<p>
	<strong><span style="color:#b22222;">Lanka Tobacco </span></strong></p>
<p>

Lanka tobacco is exclusively grown on the banks and deltaic islands of Krishna and Godavari rivers in Andhra Pradesh. The soils are derived from the recent alluvium deposited annually by the rivers Krishna & Godavari during floods and range from sandy to loam in texture. Some of these soils, which are submerged during floods every year are silty in nature and therefore highly fertile. These soils also conserve lot of moisture and tobacco is cultivated year after year on such low lying lankas. Lanka tobacco raised on high level lands on the other hand, requires two or three irrigations.</p>
<p>
	<strong><span style="color:#b22222;"><em>Bidi</em> Tobacco </span></strong></p>
<p>


In Gujarat, bidi tobacco is grown in Charotar area comprising of Anand, Petlad and Nadiad taluks of Kheda District and in some parts of Vadodara District. The soils in this tract are mostly sandy to sandy loam called "Goradu". The Bhatha lands of the rivers Mahi and Sabarmathi are considered to be the best soils for bidi tobacco. It is mostly grown under irrigated conditions. In Karnataka, bidi tobacco is mainly grown in Nipani area of Belgaum District on the banks of river Krishna and its tributaries. Hence the crop is grown on black silt loams which have good moisture retentive capacity. In Karnataka bidi tobacco is grown as  a rainfed crop though in Nipani area it is grown under irrigation from wells. Bidi tobacco is also grown to some extent in Kurnool District of Andhra Pradesh. Soils used for cultivation of bidi tobacco are heavy soils ranging from silt loam to clay and crop is raised on conserved moisture mostly as rainfed crop</p>

<p>
	<strong><span style="color:#b22222;">Chewing and Hookah Tobacco </span></strong></p>
<p>

Chewing and hookah types of tobacco are grown on different types of soils ranging from sandy loam upland soils of Vaishali, Samastipur, Muzaffarpur to medium and heavy paddy growing soils of Purnea, Katihar and Saharsa. But the main tobacco growing area in north Bihar consists of non-saline calcareous sandy loam soils of alluvial type with soil pH ranging from 7.5 to 8.5. The content of calcium carbonate in these soils is as high as 45%. The presence of sufficient fluffy calcium carbonate particles in these soils provides obstruction to moisture evaporation from the soil surface. As a result the soil moisture holding capacity of these soils is quite good, because of which good tobacco crop is grown in certain pockets even without supplementary irrigation.</p>

<p>
The chewing tobacco cultivation in Tamil Nadu is concentrated in the districts of Coimbatore, Salem, Thanjavur, Madurai, Tirunelveli and South Arcot. Chewing tobacco in Tamil Nadu is grown in a wide range of soils. But in any location, soils are comparatively alkaline in nature and the crop is irrigated with waters having high salt content </p>
<p>
In Gujarat also chewing tobacco is grown in an area of 6000 ha mainly in Kaira district. The chewing tobacco of this tract is known as Lal Chopadia.</p>

<p>
	<strong><span style="color:#b22222;">Wrapper, Filler, Jati & Motihari Tobacco In North Bengal</span></strong></p>
<p>


<em>Motihari (N. rustica)</em> and  <em>jati</em>, wrapper and filler (N. tabacum) tobacco types are cultivated in an estimated total area of 14,000 ha concentrated in Cooch Bihar, Jalpaiguri, Mushidabad and Malda district of North Bengal region. The soils of the area are alluvial flood plains formed from the material deposited by the Ganga and Brahmaputra river waters. The soils are sandy loams and silt loams, light in texture, whitish grey to greyish in colour, well-drained and well-aerated. The water and nutrient holding capacity of the soils is poor because of their coarse texture. Soils of North Bengal are of recent origin, and are less weathered. The alluvial sandy loams of North Bengal are acidic in soil reaction (pH 5.1-6.4), high in available P status and low to medium in available K</p>


