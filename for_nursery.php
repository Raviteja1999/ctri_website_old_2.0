<h1 style="text-align: center; ">
	<span style="font-size:16px;"><span style="color: rgb(165, 42, 42); "><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;NURSERY MANAGEMENT</strong></span></span></h1>

<p>Tobacco seeds are very small and egg-shaped with thick seed- coat. They are about 0.75 mm long, 0.53 mm broad and 0.47 mm thick. Depending on the variety and the conditions under which the seed is produced, the size and the weight of the seed vary considerably. In N. tabacum the average weight of the seed is 0.08 to 0.09 mg and there are 11,000 - 12,000 seeds per gram. In N. rustica, the seed is larger and about three times heavier. The emerging seedlings are tiny and delicate and therefore, the seeds are unsuitable for sowing directly in the field. Hence they are sown in small areas called nurseries or seed beds and tended carefully till the seedlings attain a particular size before transplanting in the main field. For successful raising of nurseries proper location, good preparation and manuring, adequate facilities for watering and timely controlling of pests and diseases are essential.</p>
<table>
  <tr>
    <td><img src="images/ff_nursery1.jpg" width="155" height="123" alt="nursery1"></td>
    <td class="generaltext">Generally tobacco nurseries are grown on sandy or sandy loam soil. The cigarette-tobacco growing areas of Andhra Pradesh are an exception in that the crop is grown on a heavy black soil and the nurseries are generally raised on sandy to gravely loams. Raising of nurseries on heavy black soils is hazardous due to poor internal drainage caused by high clay content coupled with prevalence of heavy rainfall and high temperatures, pre-disposing the crop for disease like damping off.</td>
  </tr>
</table>
<p>The nursery site should have a good internal as well as surface drainage and should be situated at an elevated place. Growing of a green manure crop like dhaincha or sunnhemp for 6 to 7 weeks and ploughing it is also practiced in places like Dinhata (West Bengal).</p>
<table>
  <tr>
    <td><img src="images/ff_nursery2.jpg" width="152" height="90" alt="nursery2"></td>
    <td class="generaltext">It is desirable to change the nursery site every year as it would minimise incidence of pests and diseases and also eliminate contamination by other varieties. If it is not possible to change the site, old site can be used after sterilizing by rabbing, i.e. by burning any readily available slow-burning waste-materials like, tobacco-stalks, paddy-husk, sugarcane-trash, etc. For the best results this operation should be done at the right moisture content, after the final preparation of the bed and a few days before sowing.</td>
  </tr>
</table>
<p>Systematic layout of nursery on raised beds with intervening channels helps in quick drainage of rain water. The beds are 1.0 m to 1.22 m wide to facilitate hand-weeding and watering with rose cans. The beds can be of any convenient length along the slope but generally not more than 10 m. The channels are to be half metre wide and 10 cm deep. Mixing of sand at 100 to 200 tonnes/ha in the preparation of the beds, helps in improving the drainage in heavy soils.</p>
<p>Application of FYM or filter press cake @ 25 tonnes/ha by mixing well in the top layers atleast 20 days before sowing was found to be beneficial in obtaining more number of transplantable seedlings. Basal application of 50 gm of ammonium sulphate, 50 gm of potassium sulphate and 300 gm of superphosphate and 100 gm of dolomite per 10 sq.m. bed is recommended. After germination of seed top dressing of ammonium sulphate twice at 4 days interval @ 25 gm/10 sq.m. and thereafter thrice at 4 days interval @ 50 gm/10 sq.m. and twice potassium sulphate @ 25 gm/10 sq.m. is recommended. After each pulling top dressing with ammonium sulphate @ 100 gm/10 sq.m. bed is to be given to boost the growth of the remaining seedlings.</p>
<p>The seed rate is dictated by the climatic conditions. The optimum seed rate is 3-5 kg/ha (0.4 g/m2) in general. A seed-rate of 3 kg/ha for N. tabacum and 6 kg/ha for N. rustica was found to be optimum under Dinhata (West Bengal conditions).</p>
<p>The sowing time of FCV tobacco of Karnataka is March and FCV and other tobaccos in other areas are 1st to 2nd fortnight of August. The first ten days of nursery period is most critical and if the afternoons are hot, missing even one watering can cause severe damage to the germination of seed. Almost all the sowings are done when the day temperatures are high. The provision of covers and mulches over seed beds have been found very beneficial as they conserve moisture during germination and later protect the tender seedlings from strong sunlight and beating rains.</p>
<table>
  <tr>
    <td><img src="images/ff_nursery3.jpg" width="155" height="116" alt="nursery3"></td>
    <td class="generaltext">In order to prevent the seedlings from desiccation due to intense heat and beating rains, under Dinhata conditions bamboo mats or jute-stick covers are placed directly on the beds while in Pusa the seed beds are covered with grass thaties or dry grass spreads. At Hunsur, paddy-straw covering was found to give adequate protection to the seed. Covering the tobacco nursery beds with coir mat pandal gave 21.9% increase in transplantable seedlings over geotextile or paddy straw and also saves two waterings per day.</td>
  </tr>
</table>
<table>
  <tr>
    <td><img src="images/ff_nursery4.jpg" width="155" height="106" alt="nurser4"></td>
    <td class="generaltext">Covers are gradually thinned or removed from the day when the seedlings appear on the surface as otherwise the seedlings may become etiolated and lanky for want of sunlight. It is also advantageous to transplant the seedlings from one to another bed i.e., resetting, when they are 3 to 4 weeks old in order to ensure sturdy growth of seedlings by the time of planting. A week to ten days before planting, the seedlings are hardened by withholding water. Such seedlings withstand the shock of transplanting better than normal seedlings.</td>
  </tr>
</table>
<table>
  <tr>
    <td><img src="images/ff_nursery5.jpg" width="155" height="112" alt="nursery5"></td>
    <td class="generaltext">Seedlings of pencil thickness and of 10 to 15 cm length normally are preferred. Short seedling may establish well under optimum conditions in heavy soils. In light soils longer seedlings are preferred for planting. Normally, the seedlings are ready for planting at the end of 7th week and in the first picking 30 to 40% of the total seedlings will be available. The seed bed is top-dressed after each pulling to make the remaining seedlings also grow to transplantable size.</td>
  </tr>
</table>
<p>When planting is delayed due to unfavourable field conditions, the overgrown seedlings in beds are clipped without damaging the growing point in order to retard the growth.</p>

