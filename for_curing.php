<h1 style="text-align: center; "><span style="font-size:16px;"><span style="color: rgb(165, 42, 42); "><strong>TOBACCO CURING</strong></span></span></h1>
<input type="button" id="btn" value="Print" onclick="printDiv();" style="float:right">
<div id="content">
<br>
<p >
	Curing is a process by which the harvested tobacco leaf is made ready for the market. It is a well standardized process especially in FCV tobacco to achieve the desirable qualities in the cured leaf along with the removal of moisture. The process of curing has an intimate bearing on the quality of cured leaf. A good quality leaf from the field can be made poor by an improper curing. The curing operations followed in India are dependent on type of tobacco, the mode of consumption, local preference, convenience, market value and production economics. Depending on the type of the tobacco principal methods of curing can be distinguished as i) flue-curing, ii) air-curing, iii) fire-curing and iv) sun-curing. Though some of the characters that govern quality in tobacco are genetic, the way of practicing and following standard curing methods play a significant role in determining the quality of leaf. </p>
<p style="text-align: center; ">
	<span style="color:#b22222;"><strong><span class="generaltext">FLUE-CURED VIRGINIA TOBACCO </span></strong></span></p>

<p >
	<span style="color:#800000;"><strong>Harvesting:</strong></span></p>
<p>
	Uniform, well-matured and ripe leaves should be harvested. Ripe leaves have greenish-yellow colour with velvety feel and less sticky. Matured leaf lie horizontally or bend slightly down with tips slightly dry. As a general rule, the leaves are harvested from the bottom primings slightly on the green side, the middle leaves when they are ripe and the top leaves when they are fully ripe. On an average, not more than three leaves should be harvested at a time. Harvesting must be done on a clear weather day. Immediately after rains or irrigation the crop should not be harvested and it is to be delayed by 2 - 3 days in such cases. Under normal condition, priming is done once in a week. The leaves should be pluck against the direction of the sun for better judgment of matured leaf colour. While picking, the midribs should be bent side-ways and a well-matured leaf will snap crisply with a characteristic sound. The leaves are to be carried carefully without pressing in a wide basket with tips upward and shifted to tying shed immediately to minimize the possible wilting.</p>
<p >
	<span style="color:#800000;"><strong>Green-leaf grading: </strong></span><br />
	In spite of utmost care, during the harvest some immature leaf and over ripe leaves may be picked which have to be sorted out before tying. These over ripe and under-ripe leaves must be tied separately so that each stick contains leaves of uniform colour. Over-ripe leaves are usually yellowish white and under ripe leaves are relatively dark green.</p>
<p >
	<span style="color:#800000;"><strong>Tying the leaves: </strong></span><br />
	The leaves are to be tied to sticks by handling gently in a shaded place avoiding wilting and bruising. A bruised leaf (physically damaged) does not cure well in the barn. About three leaves are tied in a bunch, back-to-back, with a jute twine loop on a stick. About 90-100 such leaves are tied in separate bunches with a series of loops on a stick approximately 130 cm long. The leaves are distributed uniformly all over the length of the stick to avoid overcrowding.</p>
<p >
	<span style="color:#800000;"><strong>Loading the barn: </strong></span><br />
	For a satisfactory curing, the whole barn should be loaded with the freshly harvested leaves from a single priming. The un-ripe leaves (green) are placed on the top tiers, the over-ripe leaves (yellowish-white) leaves on the bottom tier and well-matured leaves (greenish-yellow) in the bulk of the intermediate tiers. The sticks are placed on the tiers approximately at a distance of 20-25 cm so that the leaves from the adjacent sticks slightly touch each other without pressing. A 5m x 5m x 5m barn will usually be loaded with 750 sticks with such spacing. The barn should never be over loaded while curing the bottom and middle leaves since they spoil easily if drying rate is slow. Leaves from the top of the plant may be crowded slightly more by closer spacing without much detriment to grade-return. Loading of the barn should be completed by late afternoon.</p>
<p >
	<span style="color:#800000;"><strong>Curing practice: </strong></span><br />
	Curing virginia tobacco according to fixed schedule is not possible all the time because of the variability in green leaf due to various factors like weather condition, plant position, leaf maturity, disease prevalence and in such cases slight adjustment in the process is necessary. The schedule will only serve as a broad outline and individual judgement will be necessary in almost every case.</p>
<p>
	<span style="color:#800000;"><strong>Curing principle: </strong></span><br />
	The early stage of flue-curing should permit continuing biological activity in the leaf permitting destruction of chlorophyll, conversion of starch to simple sugars and leaf proteins to soluble nitrogenous constituents. These cellular reactions take place in fully turgid leaf cells in aqueous medium and for complete enzymatic reactions, thermal inactivation of these enzymes must be prevented. This means maintaining high humidity and low temperature in the barn for these favorable reaction sequences. During this period leaf turns yellow containing high percentages of soluble sugars. At this juncture further breaking up of sugars by respiratory enzymes must be prevented as cured leaf must contain high sugars. Browning reaction caused by polyphenol oxidase turning the yellow leaf to browns and bio-chemical conversion of soluble nitrogen to ammonia must be arrested as some of these transforms into aroma compounds at a later stage. These are achieved by thermal desiccation by progressively raising the temperature of the barn and lowering relative humidity by ventilation adjustments during subsequent stages and abrupt change in temperature and humidity should never be made in the barn while curing. </p>
<p >
	&nbsp;</p>
<p >
	<strong><span style="color:#800000;">Details of the improved curing method are given below:</span></strong><br />
	<br />
	<strong>1. Yellowing:</strong><br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Temperature - Dry bulb 85-105 &deg;F.<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Wet bulb 82-94&deg;F.<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Time - 36-48 hours.<br />
	<br />
	Furnace is charged after loading the leaf and temperature raised by 5 to 6&deg;F above outside temperature. Top ventilator is left very slightly open, especially during the cooler hours of the night, bottom ventilators are left open with slight gaps so that upward movement of air continues in the barn.<br />
	Temperature is raised by not more than 1 to 2&deg;F per hour reaching 105&deg;F by the time the leaf becomes yellow and is ready for fixing. Temperature should be raised in such a way that yellowing is nearly completed by the time it reaches 105&deg;F. Top and bottom ventilators are gradually opened to 3 or so.</p>
<p >
	<strong>2. Fixing colour:</strong><br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Temperature - Dry bulb 105-120&deg;F<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Wet bulb 94-98&deg;F<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Time 5-10 hours<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Progressive total time 39-47 hours.<br />
	<br />
	Great care is required in raising the temperature during this stage. It is raised by not more than 1 to 2&deg;F every hour. Bottom ventilators are opened to 3'' - 5'' at the base. Top ventilator is raised to a height of 3''- 5'' from the roof. It is not necessary to raise the top ventilator completely.</p>
<p >
	<strong>3. Leaf drying:</strong><br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Temperature - Dry bulb 120 to 145&deg;F<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Wet bulb 98 to 110&deg;F<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Time : 36-48 hours<br />
	<br />
	After getting 130&deg;F the top ventilators are gradually closed followed by bottom ventilators and all the ventilators remain closed after reaching 140&deg;F.</p>
<p >
	<strong>4. Midrib drying:</strong><br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Temperature - Dry bulb 145 to 160&deg;F<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Wet bulb 110 to 114&deg;F<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Time : 24-36 hours<br />
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Progressive total time: 88 to 101 hours.<br />
	<br />
	Temperature is raised and maintained at a maximum of 160&deg;F until the stem is dry. The ventilators closed during leaf-drying stage continued to be kept in the same position.</p>
<p >
	<span style="color:#800000;"><strong>Unloading the barn: </strong></span><br />
	The barn is allowed to cool down keeping the ventilators closed after the completion of curing and the fire is put off. To attain proper condition for handling of the leaf all doors and ventilators are wide open at night to allow the leaf to absorb required moisture from the atmosphere. If dry weather prevails keep wet gunny bags on flue pipe with very low fire in the furnace to build up humidity inside the barn for few hours just enough for leaf handling. The sticks are removed from the barn and kept in the racks. The leaf is untied from the sticks when there is proper condition preferably during early morning hours and bulked.</p>
<p >
	<span style="color:#800000;"><strong>Bulking the cured leaf: </strong></span><br />
	Bulk must be made with a view to efficient grading. Leaves of different varieties must be bulked separately. Leaves from different stalk position must also be kept separate if space in the shed permits for separate bulks. Otherwise, leaves may be bulked together with adequate identification marks for each priming (paper markers may be placed in between two primings). The bulk must be about one meter high on a raised platform and never be on the floor or near materials with offensive odour like insecticides, fungicides or fertilisers. Moisture condition of the bulk must be monitored periodically. Too dry bulk lead to shattering of leaf and over-conditioned leaf will lose colour and may become moldy. The correct condition can be judged by handling a bunch of cured leaves and pressing them tight in the fist of the palm. It is over-conditioned if the leaves do not spring back after release of pressure and if it breaks then it is under-conditioned. The bulks must be covered with polythene sheets (or some other suitable covering e.g., tarpaulin) and weight down sufficiently to compact the bulk to prevent gain or loss of moisture. The bulk is turned for approximately 2-3 times prior to grading depending on the moisture content.</p>
<p >
	<span style="color:#800000;"><strong>Grading: </strong></span><br />
	Grading is one of the important production practices in flue-cured Virginia tobacco production. It is the manual sorting of cured leaves into homogeneous lots according to the plant position styles, for manufacturing purpose, and external appreciation. The parameters considered include colour, blemish, damage, texture, leaf length and ripeness. These will be applied to various degrees, depending on the tobacco type, market requirements and to each plant position style. In black soils farm grades and in light soils plant position grading is practiced. The main purpose of farm grades in black soil tobacco and plant position grades for light soil tobacco is to avoid dual grading at farmer's level and trader's level and thus reduce the marketing cost. This helps growers to realize more income for his produce, saves time for the exporters, helps government in fixation of minimum support price and minimum export price for each grade.

<br />
	</p>
