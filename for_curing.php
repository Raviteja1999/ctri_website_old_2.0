<p class="generaltext" style="text-align: center; ">
	<span style="color:#800000;"><strong>Lanka tobacco </strong></span></p>
<p>
	<span style="color:#800000;"><strong>Harvesting: </strong></span></p>
<p>
	The crop gets ready for harvest 90-100 days after planting. Harvesting in the evening hours by cutting the plants close to the ground when the tips of leaves begin to dry and show considerable thickening and puckering. The plants are left over night in the field to wilt. The plants are brought nearer to curing-shed in the early hours of next day and then the leaves are separated along with a big slice of the stem. The 10-15 wilted leaves are stitched to the Agave fibre with the help of tobacco needle (a long iron needle).  These are called Thoranam&#39s; or Chekka&#39s;.</p>
<p>
	<span style="color:#800000;"><strong>Shade curing:</strong></span></p>
<p>
	These Thoranams&#39s; or Chekkas&#39s; (bunches) are to be arranged on coir ropes and the ropes are to be tied to bamboo racks for a period of 45 to 60 days in a curing shed of the size 48&#39; x 5.5&#39; x 2.5&#39;.</p>
<p>
	<span style="color:#800000;"><strong>Pit Curing </strong></span></p>
<p>
	Bundles of (Thoranams) shade-cured leaves are pit cured in the pits of 8 feet diameter and 3 feet depth by keeping them in circular layers upto half of the pit and then covered by palmyrah leaves and soil, made it air tight. After 24 hours, the leaf (Thoranams) has to be transferred to another pit of the same dimensions, filled and covered in the same way as the first one and kept for 48 hours. Later, the leaf (Thoranams) is to be transferred to the 1st pit in the same way and kept for 24 hours. This transferring process is to be done at nights crescent moon light to avoid loss of moisture.</p>
<p>
	<span style="color:#800000;"><strong>Bulking: </strong></span></p>
<p>
	The leaf is then brought to a shed and bulked. The bulks are to be turned on alternate days till the leaves develop dark brown colour and pungent odour.</p>
<p >
	<span style="color:#800000;"><strong>Grading: </strong></span></p>
<b>Grading is done as per the following grades</b>
<p >
	1. Kotaku: Top two well developed leaves of the plant</p>
<p >
	2. Baraku: Next two well developed leaves</p>
<p >
	3. Mattasam: The next two or three middle leaves</p>
<p >
	4. Gulla or Tepa: Lower leaves, poor in body and size.</p>
<p >
	Lanka tobacco has an excellent aroma and physical characteristics suitable for manufacture of high quality cheroots.</p>
<p class="generaltext" style="text-align: center; ">
	<span style="color:#800000;"><strong>Cigar Wrapper tobacco </strong></span></p>
<p>
	<span style="color:#800000;"><strong>Harvesting and curing </strong></span></p>
<p>
	Wrapper tobacco takes about 120-140 days from the date of planting till it gets ready for final harvest. Matured leaves of yellowish-green colour with light brown spots (spangles) are harvested by priming (Removing 4 or 5 leaves at each priming in four or five installments). Cigar wrapper is an air-cured tobacco. Harvested leaves are placed in katcha curing barn on racks made of bamboos after stringing the leaves with the help of sutli. The leaves are cured under atmospheric temperature and 70 - 80% relative humidity. The process takes 5-6 weeks for completion. Air-curing consists of two stages. During first stage, leaf remains alive. To secure a satisfactory cure, the leaf should remain alive till the necessary changes takes place. The harvested leaf is rich in starch. One of the important changes during curing is the disappearance of starch. If the leaf is dried too rapidly the starch is not removed and tobacco is degraded. The green colour changes to colourless and the yellow colour appears. The yellow colour marks the end of the first stage. One of the most important changes during the second stage of curing is the development of brown colour which is due to oxidation and does not take place till the leaf cells are dead. The two essentials for the development of brown colour are oxygen and moisture. Brown colour development is completed in fermentation.</p>
<p>
	After leaf is dry, it is allowed to come to proper condition for handling. Then, it is tied into hands and bulked on a raised platform. The bulks are broken frequently, hands shaken and the bulks are remade. This frequent shaking and rebuilding of the bulks is necessary to replace any ammonia and carbon monoxide formed during bulking by atmospheric oxygen and excessive rise in temperature.</p>

<p class="generaltext" style="text-align:left; ">
	<span style="color:#800000;"><strong>Grading : </strong></span></p>
<p >
	The desirable quality characteristics in wrapper tobacco are uniform light brown colour, thin to medium and pliable texture, smooth and glossy appearance, thin veins and good leaf length.</p>
<p>
	The cured wrapper tobacco can be classified into 4 grades.</p>
<p >
	1st grade : Light brown coloured leaves, fine in texture with smooth and glossy appearance and with a leaf length of 40 cm (16) or above</p>
<p>
	2nd grade: Light brown coloured leaves good in texture with glossy appearance and length 30 cm to 40 cm (12 to 16)</p>
<p>
	3rd grade: Light brown coloured leaves, medium in texture and length 22 cm to 30 cm (9 to 12)</p>
<p>
	4th grade: Light brown to dark brown coloured leaves, medium in texture and length below 22cm (less than 9). Also torn and slightly damaged leaves of above three grades are classified as fourth grade.</p>
<p class="generaltext" style="text-align: left; ">
	<span style="color:#800000;"><strong>Sun-curing </strong></span></p>
<p>
	Method of sun curing is practiced for different types of tobacco in India. This method is traditional/local and is relatively quick (2-3 weeks) with little interference from weather changes. Below are the different types of sun-curing practiced in different parts of the country.</p>
<p>
	a. <b>Curing whole plant on racks:</b> Cigar and chewing tobaccos of Tamil Nadu.</p>
<p>
	b. <b>Curing leaves together with pieces of stalk on racks:</b> <em>Natu</em> tobacco in Andhra Pradesh.</p>
<p>
	c. <b>Curing whole plant on the ground:</b> Bidi tobacco of Gujarat, Hookah and chewing tobacco in Bihar.</p>
<p>
	d. <b>Curing primed leaves on the ground:</b> As chewing tobaccos in Uttar Pradesh and Hookah tobacco in West Bengal.</p>
<p class="generaltext" style="text-align: center; ">
	<span style="color:#800000;"><strong>CHEWING AND HOOKAH TOBACCO IN BIHAR </strong></span></p>
<p>
	<span style="color:#800000;"><strong>Harvesting and curing </strong></span></p>
<p >
	Crop is harvested at full maturity stage by stack cut method with the help of a sickle or khurpi close to the ground level. After harvesting, crop is left in the field for sun-drying for 4 to 6 days. After 2-3 days drying, plants are turned upside down for drying of remaining portion. After 5-6 days of sun drying harvested crop is brought to the curing yard i.e. clean grassy patch and after two days of drying it is turned upside down in the morning. As such it is left for drying for four days, then bigger size heaps (bulk) are formed. After four days, the heap or bulk is broken (dismantled) spread on grass patch where it is allowed to dry for four days and then it is turned and dried for two days. This process of heaping, spreading and drying, turning and re-heaping/bulking and exposing to dew in the night continues for about one and a half month till leaves become dark brown, stalks dry up and loose green colour.

</p>
<p >
	After separation of leaves from the stalk, bundles of 25 to 50 leaves each are formed by tying it with the help of banana fibre. If stalk bits attached to the leaves are not dried up, the bundles should be kept in the sun for 3-4 days for complete drying, otherwise it can be directly kept in the shed. Bundles of the leaves should be arranged into a heap or bulk. This bulking and rebulking should be done 3-4 times at a week interval depending on the moisture condition in leaves and heat generation in the bulk/heap.</p>
<p>
	Watering and fermentation is most important stage in curing process, because excess watering may spoil the colour and aroma of the leaf and less watering may depress the formation of white encrustation (deposition of sulphate and chlorides of Na, K, Mg, Ca etc.) on the leaf surface. Sunny weather with dry winds or westerly winds are most suitable for watering purpose because even little more watering also will not cause any harm. However, watering and fermentation process should be handled by experienced person or master curer only in order to ensure good fermentation and preparation of high quality leaf.</p>
<p>
	<span style="color:#800000;"><strong>Grading </strong></span></p>
<p>
	After completion of watering and fermentation leaves are graded according to their size, thickness colour &amp; aroma.</p>
<p>
	<strong>1st grade:</strong> It is locally called Moorhan or Jotia. It consists of long, broad, thick and dark brown leaves with pungent taste and pleasant aroma.</p>
<p>
	<strong>2nd grade:</strong> It is locally called male moorhan. It consists of medium size leaves, less pungent than first grade and brownish in colour.</p>
<p>
	<strong>3rd grade:</strong> It is locally called Chhobuva or Chhootki. It consists of brown and small leaves with low pungency.</p>
<p>
	<strong>4th grade:</strong> It is locally called Kharsan and it consists of sand leaves of very light weight and inferior quality.</p>
<p>
	Before packing small bundles of 10 leaves each are made grade wise and two to four bundles are tied together.</p>
<p>
	<span style="color:#800000;"><strong>Packing </strong></span></p>
<p >
	Bales (packing) of 1.5 to 2.0 quintals are made with the help of thatch grass gunny bags or hessian cloth and jute rope. Before packing some water is sprinkled on the leaf bundles to maintain lustre and aroma and to protect the leaves against storage beetle. Bale packings are opened after 10-20 days and repacked. This packing may be opened again after 1 or 2 months according to condition of the leaves and repacked. This process is continued till disposal of the leaf.</p>
<p class="generaltext" style="text-align: center; ">
	<span style="color:#800000;"><strong>CIGAR FILLER TOBACCO </strong></span></p>
<p >
	<span style="color:#800000;"><strong>Harvesting and curing </strong></span></p>
<p>
	Cigar filler tobacco is harvested by priming the leaves, 4-5 at a time when leaves are mature. The cigar filler tobacco in North Bengal is a sun-cured tobacco. The harvested leaves are strung on a jute string which is then spread on a bamboo stick. These sticks are then transferred to multi-tier bamboo structures, constructed in the open space where sufficient sunlight is available for drying of leaves. After drying the leaves for 7 to 10 days, the leaves are then placed in katcha curing barn till the leaves get completely dried including midrib. On completion of curing which takes 15-20 days, the leaves are allowed to come to proper condition for handling. Then they are tied into hands and bulked on a raised platform. The bulks are broken frequently, the hands shaken and the bulks rebuilt. After bulking for about 6-8 weeks, the tobacco becomes ready for marketing.</p>
<p >
	<span style="color:#800000;"><strong>Grading </strong></span></p>
<p>
	Cigar filler is graded on the basis of leaf size, colour, texture, strength, burn and flavour. It may be classified into the following two grades.</p>
<p >
	Raasi grade: Leaves with uniform brown colour, thin to medium texture, good size, mild strength, good burning with white ash and agreeable flavour.</p>
<p >
	Cruz grade: The leaves of smaller size, brown to dark brown colour, medium texture, medium burning with whitish-grey ash and medium flavour.</p>
<p>
	<span style="color:#800000;"><strong><em>Jati</em> and <em>Motihari</em> tobaccos </strong></span></p>
<p >
	<span style="color:#800000;"><strong>Harvesting and curing </strong></span></p>
<p>
	<em>Jati</em> tobacco will be ready for harvest in about 120-130 days after planting or 50-60 days after topping. Matured leaves with yellowish-green colour and brown spots (spangles) are harvested in 2 or 3 installments by removing 2 or 3 leaves at each priming. The <em>Jati</em> and <em>Motihari</em> tobacco grown in North Bengal are cured first in sun and then in air. The primed leaves are left in the field for about 8 to 10 hours for wilting under sunlight. The wilted leaves tied into bunches of 5 to 6 leaves and cured on bamboo splinters in curing sheds for 4 to 6 weeks by which time midribs are dried. Then they are arranged in bulk and fermented. After bulking, the tobacco is graded for marketing. The quality characters desirable in <em>Jati</em> tobacco are reddish brown to brownish yellow coloured leaves with good puckering, good size and glossy appearance.</p>
<p>
	<span style="color:#800000;"><strong>Grades </strong></span></p>
<p>
	The following grades are prevalent in <em>Jati</em> tobacco.</p>
<p>
	<b>Pan patta</b> : Leaves reddish brown to brownish yellow in colour with good puckering and of uniformly good size and fine and pliable, good body and free from blemishes.</p>
<p>
	<b>No.1 (Good quality)</b>: Good body leaves, satisfactory in colour and puckering, free from blemishes, medium size with minor physical injuries are also included.</p>
<p>
	<b>Niras (Khunda)</b>: Leaves with slightly dark appearance and devoid of puckering with dry feel. Considerable physical injuries blemishes not more than 10% of the total area.</p>
<p >
	<b>Zal-patta (Fired leaves)</b>: Leaves that do not mature properly and are partly burnt before harvest. Blemishes all together not exceeding 25% of the total area and dark coloured.</p>
<p class="generaltext" style="text-align: center; ">
	<span style="color:#800000;"><strong>HDBRG Tobacco </strong></span></p>
<p >
	<span style="color:#800000;"><strong>Harvesting </strong></span></p>
<p >
	The matured leaves show good body, puckering, slightly yellowish mottling with lustre. The crop is harvested in three phases, i.e., bottom, middle and top leaves. The bottom leaves are light in colour poorly developed with low body. The middle leaves are well spread, lengthy, broad and heavy bodied, contributing a major chunk to the total leaf production, and represents good colour, aroma and texture after curing. The top leaves are sturdy, thick, woody, dark green to medium green and gives dark tan colouration </p>
<p>
	<span style="color:#800000;"><strong><span class="generaltext" style="">Curing and Bulking </span></strong></span></p>
<p class="generaltext">
	Two hundred to two hundred and fifty harvested leaves are stitched with bengal twine with the help of iron needles and allowed to sun cure on pre erected bamboo poles for about 30-35 days. Well cured leaves are characterised by good texture, lustre and pliability. The untwined leaves are bulked for about 30-35 days at 6-8 days interval with 3-5 turnings. Bulking should be done priming wise only to get good quality leaf and profitable price.</p>
<p class="generaltext">
	<strong><span style="color:#800000;">Grading</span></strong></p>
<p class="generaltext">
	HDBRG tobacco is graded priming wise only as bottom, middle and top leaf. Under each category, two types of grades are given i.e.,</p>
<p class="generaltext">
	<strong>WBR:</strong> Bright tan coloured with less blemish, good body and fully matured lengthy leaves.</p>
<p class="generaltext">
	<strong>WBL:</strong> Lower short leaves with buff colouration, weak body, moderately blemish and fairly shattery.</p>
<p class="generaltext" style="text-align: center; ">
	<strong><span style="color:#800000;">Natu tobacco</span></strong></p>
<p class="generaltext">
	<strong><span style="color:#800000;">Maturity and Harvest</span></strong></p>
<p class="generaltext">
	The matured leaves show good body, puckering, slightly yellowish mottling with lustre. The crop is harvested in three phases, i.e., bottom, middle and top leaves. The bottom leaves are light in colour, poorly developed with low body. The middle leaves are well spread, lengthy, broad and heavy bodied, contributing a major chunk to the total leaf production, and represents good colour, aroma and texture after curing. The top leaves are sturdy, thick, woody dark green to medium green and gives dark tan colouration after curing.</p>
<p class="generaltext">
	<span style="color:#800000;"><strong>Curing and bulking</strong></span></p>
<p class="generaltext">
	Two hundred to two hundred fifty harvested leaves are stitched with bengal twine with the help of iron needles and allowed to sun cure on pre-erected bamboo poles for about 30-35 days. Well cured leaves are characterised by good texture, lustre and pliability. The untwined leaves are bulked for about 30-35 days with 3-5 turnings at 6-8 days interval. Bulking should be done priming wise only to get good quality leaf and profitable price. Some varieties of irrigated natu tobacco are harvested by stalk-cut method and the stalks are hung on the pre-erected bamboo poles. The leaves are removed from the stalk after bulking.</p>
<p class="generaltext">
	<span style="color:#800000;"><strong>Grading</strong></span></p>
<p class="generaltext">
	Grading is done representing bright (CBT), brown (CBR), dark brown (CDK), green (CG) and perished leaf (CP). The Eluru toranam leaf is graded as &lsquo;Melmi&rsquo; i.e good bodied and matured leaf while &lsquo;Gulla&rsquo; is bottom damaged leaf without body.</p>
<p class="generaltext" style="text-align: center; ">
	<span style="color:#800000;"><strong>JAFFNA TOBACCO OF CEYLON AND CHEW ING TOBACCO OF TAMIL NADU </strong></span></p>

<p class="generaltext">
	<span style="color:#800000;"><strong>Fire-curing: (Smoke curing)</strong></span></p>
<p class="generaltext">
	Important type of tobacco that is fire-cured is Jaffna tobacco of Ceylon and Tamil Nadu used for chewing purpose. The leaf is harvested by either priming or stalk-cutting each leaf together with a portion of the stem.</p>
<p class="generaltext">
	The leaves are wilted for four hours in the field, tied into bundles and hung of laths in smoke huts. They are then smoked for 12 hours by burning coconut husks, leaf stalks and palmyrah nuts, stacked for 3 days and again smoked. Alteration of firing and stacking at an interval of few days helps in making the colour of leaf even. During smoke treatment the creosotic substances are deposited on leaf surface which gives it a peculiar taste. After smoking the leaves are bulked for 3-4 weeks and than given salt water/jaggery treatment prior to sale.</p>
<p class="generaltext" style="text-align: center; ">
	<strong><span style="color:#800000;">BIDI TOBACCO</span></strong></p>
<p class="generaltext">
	<strong><span style="color:#800000;">HARVESTING AND CURING</span></strong></p>
<p class="generaltext">
	Harvesting of bidi tobacco normally commences in January-February in Gujarat and December-January in Karnataka and Maharashtra. Harvesting of bidi tobacco is done at an advanced stage of maturity. The maturity is judged by pronounced development of brown spots called &lsquo;spangles&rsquo;. The leaves are ripe for harvest about 75 days after topping. Bright sunny days are ideal for harvesting. Prevalence of cloudy weather affects the quality adversely. Generally bidi tobacco is harvested in Gujarat in the following three ways:</p>
<p class="generaltext">
	i) Whole plant harvest: The mature plants are cut at the base and left in the field inverted for about three days. The drying is considered when the lamina close to the lower half of the midrib becomes brittle.</p>
<p class="generaltext">
	ii) Leaf-wise-harvest: Mature leaves exhibiting full spangle development are plucked and left in the field with the upper surface facing the ground for drying. The dried leaves are collected in a tarpaulin and the lamina (bhuka) are stripped off from the midribs. After about 4 days pieces of lamina and veins adhering to the midribs are separated and kept in a separate heap. The midribs are exposed to the sun further for about a fortnight for drying. The bark of the stalks are scrapped with a sickle, dried well and then mixed with the midribs. This mixture is locally known as lakhada. The bottom 2 or 3 leaves called gabia are also collected and dried and heaped separately.</p>
<p class="generaltext">
	iii) Gugro method: This method is practiced in seasons of deficient rainfall when the leaves do not mature well and there is drying of the leaf along with the midrib. The green lamina of the mature leaf of the standing crop is stripped from the midrib by passing the thumb and the index finger from the base to the top of the midrib. The lamina pieces are spread on the ground for sun drying with the top side facing the ground. Later the stalks are cut and the midribs are separated out from the plants.
In Karnataka and Maharashtra harvesting of bidi tobacco is done by stalk cut method. The whole plants are cut and kept inverted in the field for 6-7 days for sun curing. When dry, the lamina is separated out from the main stem. The midribs are further dried to remove the remaining portion of the lamina. This is Angad tobacco sold in the market.
</p>

<p class="generaltext">
	<span style="color:#800000;"><strong>GRADING</strong></span></p>
<p class="generaltext">
	At grower&rsquo;s level there is no standard system of grading of bidi tobacco. Grades vary according to merchants, manufacturers and also from place to place. Bidi tobacco is valued on the basis of (1) its physical characteristics viz. colour, thickness, size, spangling of the leaf, granulation, brightness or lustre (2) its smoking qualities viz. strength, sweetness and aroma and (3) its burning qualities viz. consistency in burning, ash colour, etc. The purchasers prefer tobacco to be mild in strength and sweet in flavour; it should not leave any after effects in the mouth. Parrot green colour is most sought after. Green, golden yellow and red are also recognised. In texture, the purchasers look for granular texture-fairly thick but not coarse. Spangling is another attribute of quality. In Nipani area of Karnataka, brownish red tobacco with a greenish cast is sought after. Though there is no systematic grading method prevalent in the country, there is, however, some sort of mixing and blending of different types for the preparation of bid patti (flakes).</p>
<p class="generaltext">
	In Gujarat, bidi tobacco is sorted out into the following four grades as per commercial requirements:</p>
<p class="generaltext">
	<b>Bukha:</b> This represents the pure lamina portion only. It forms about 55-60 per cent of the produce.</p>
<p class="generaltext">
	<b>Second grade:</b> This represents a mixture of lamina pieces and veins which are separated after second drying of the midribs or the plants.</p>
<p class="generaltext">
	<b>Galia or Sand leaves:</b> These are the bottom leaves of the plant. They form about 10-15 per cent of the total produce.</p>
<p class="generaltext">
	<b>Lakada or midribs:</b> This forms about 17-20 per cent of the produce. Bulk of the produce is sold by the farmers in the form of broken leaf i.e. as Bukha.<br />
	In Karnataka at the primary level bidi tobacco is graded in terms of I sort, II sort etc. on the basis of the physical characteristics like leaf colour, luster, thickness, spangling and smoking qualities like strength, aroma etc. Before manufacture, bidi tobacco is processed and processing is done in two stages<br />
	(i) Angad into Jarda and (ii) Jarda into Jardi.</p>
<p class="generaltext">
	In Maharashtra, generally the tobacco is graded at the primary level into grades I, II, III and IV. Thick leaves with golden yellow or parrot green colour with copious spangle development and capable of giving a strong aromatic smoke are classified as Grade I, Brown, red and blackish leaves are graded as Grades II, III and IV. Other quality characteristics will also be progressively less in these grades.</p>

