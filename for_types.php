    <h1 style="text-align: left; ">
	<span style="font-size:16px;"><span style="color: rgb(165, 42, 42); "><strong>BOTANICAL CLASSIFICATION OF TOBACCO</strong></span></span></h1>

<p>
	The genus <em>Nicotiana</em> is one of the five large genera of Solanaceae and is represented by about 70 recognised species.<br />
	<br />
	Description is available for all the 60 species, which are grouped in three sub-groups:</p>
<ul>
	<li>
		<span style="text-align: center; ">Sub-genus - Rustica</span></li>
	<li>
		<span style="text-align: center; ">Sub-genus - Tabacum</span></li>
	<li>
		<span style="text-align: center; ">Sub-genus - Petunioide</span><span style="text-align: center; ">s</span></li>
</ul>
<p>
	The original habitat of the genus is considered to be South American, particularly the regions surrounding the Andes. There are, however, several species in Australia and South Pacific Islands that do not occur in the new world. They are considered to be derivatives of the South American stock. Out of the 70 species, only two species, i.e. <em>Nicotiana tabacum</em> and <em>Nicotiana rustica</em> are cultivated extensively. India grows both the species, but by far the largest area is under N. tabacum. Since N. rustica requires cooler climate, its cultivation is confined mainly to the northern and north-eastern areas of the country, i.e., U.P., West Bengal, Bihar and Assam</p>
<p>
	The <i>N. tabacum</i> varieties known as desi types have tall plants with broad leaves and have usually pink flowers. The <i>N. rustica</i> varieties known as `vilayati&#39; and `calcuttia&#39; are characterised by short plants with round puckered leaf and yellow flowers. Specific varieties in <i>N. tabacum</i> have been developed for cigarette, cigar and cheroot, bidi, hookah and snuff tobaccos. The varieties developed in <i>N. rustica</i> are used for only chewing, hookah and snuff tobaccos. In addition to <i>N. tabacum</i> and <i>N. rustica</i> which are commonly cultivated, many species of Nicotiana, such as <i>N. affinis</i>, are grown for ornamental purposes. Some of the other species were used in old days by aboriginals (Red Indians and Australian natives) for chewing and smoking. There are 70 <i>Nicotiana Sp.</i>available in the world, of which 45 are being maintained in India. Several of these species have resistance to pests and diseases and are being used for developing disease/pest resistant tobacco varieties.</p>

