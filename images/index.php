<p style="font-size: 12px;">

	Government of India constituted Indian Central Tobacco Committee (ICTC) in 1945 keeping in view the important role played by tobacco in national economy and employment generation.  The Central Tobacco Research Institute was established in 1947 at Rajahmundry under the aegis of ICTC, Madras.  The function of CTRI was taken over by the Indian Council of Agricultural Research (ICAR) in 1965.  CTRI has six regional Research Stations located at Guntur, Kandukur, Jeelugumilli, Hunsur, Vedasandur and Dinhata and a research centre at Kalavacharla.   All-India network project on tobacco was sanctioned in 1971 has 4 main centres, 7 sub centres and 4 voluntary centres to carry out the multi-locational trials on various types of tobacco. 

Tobacco is one of the important high value commercial crops grown in an area of 0.45 million hectares over 15 states in India.  India stands second in production and export of tobacco in the world. It provides employement directly and indirectly to 45.7 million people and contributed as much as Rs.22,737 crore as excise duty and Rs.5,975 crore in terms of foreign exchange to the National exchequer, during 2016-17. FCV, bidi, hookah, chewing, cigar-wrapper, cheroot, burley, Oriental, HDBRG, Lanka, Pikka, Natu etc. are the main types of tobacco grown in the country, with FCV and Burley tobacco being the main exportable types.

