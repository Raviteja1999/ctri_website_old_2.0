	<strong><span style="color:#b22222;">DIVISION OF CROP PRODUCTION</span></strong></p>
	
<p>
	Division of Crop Production comprises Agronomy, Agrl. Extension and Computer Application disciplines. Division conducts research on integrated crop management practices, technology assessment and transfer, impact and constraint analysis and software technologies for higher production efficiency of different FCV and non FCV tobaccos and for enhanced Farm returns. Developed Good Agricultural practices (GAPs) were transferred to the farming community of different tobacco domains by innovative approaches for sustainable tobacco production. Full proof technology modules for tobacco nurseries including water use efficient micro-irrigation systems were developed and transferred to the farming community. In field crop management, management practices viz. poly tray seedling production, integrated nutrient management, organic farming, integrated farming systems, region specific fertilizer recommendation, watershed management, Drip fertigation, integrated weed management and alternative uses of tobacco were developed and recommended to the needy clientele. Since, flue curing accounts to 30% of cost in FCV tobacco production, technologies for fuel saving viz. low profile barn, insulation with thermocole or rock-wool or straw-mit, redesigned the arrangement of barn flue pipes, modified barn furnace, alternative fuels to wood and coal were developed to reduce  cost of curing and to increase the net returns. 
Research work on burley tobacco is being taken up in Vinukonda area. Advisories on Good agricultural Practices are made and training programmes are conducted to burley tobacco growers and field officers of tobacco trade.  During 2019-20 season, 1000 kg seed is produced for distribution to farmers.
Tobacco is a high value cash crop. Hence, systems approach involving cropping /farming systems were found as next best viable alternative option to tobacco. Hence, cropping systems/ crops for different agro-ecological sub-regions have been developed and recommended.</p>
<p>
Technology evaluation and  impact analysis was carried out in different tobacco types and need based capacity building programmes through trainings, field days, diagnostic visits, exposure visits etc. were conducted for the benefit of farming community, officials of Tobacco Board and Industry. In line with ICT, expert systems and decision support systems were developed for tobacco.<//p>
<p>
Research under progress is focussed on cost cutting  and production efficient measures in tobacco cultivation, integrated weed management including Orobanche, crop intensification and diversification, constraint analysis, socio- economic impact, mobile apps and e-advisory portal.

