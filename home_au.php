<p>	Demonstrated low cost fruit ripening chamber which is eco-friendly, safest and low cost technology for ripening of mango, papaya, banana and others.</p>
<p>	A cheap diagnostic tool for Sub Clinical Mastitis (SURF FIELD MASTITIS TEST) was demonstrated (1 lit. of water + 30  gm.of detergent powder) when added to milk gets curdled if milk sample is positive for Mastitis. Out of 130 samples tested 20 observed positive for Mastitis.</p>
