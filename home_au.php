	 <h5 style="text-align: center; ">
	<span style="font-size:14px;"><span style="color: rgb(165, 42, 42); "><strong>MAJOR ACHIEVEMENTS (2019-20)</strong></span></span></h5>
	
<p>
	<strong>&nbsp; On Farm Trials (OFTs) &nbsp;</strong> </p>
<ul>
	<li>
	
During the year 2019-20, a total number of 9 technologies were assessed by conducting on-farm trials at farmers&#39; fields. </li></ul>
<p>
	<strong>Highlights of OFTs: &nbsp;</strong>
</p>


<p>In Rice MTU-1172 and MTU-1190 were assessed for tolerance to submergence and also as an alternate to BPT in irrigated uplands of the district.   The varieties  performed well with 45 q/ha and 52 q/ha with a benefit cost ratio of 1.70 and 1.97 over Swarna (43.2 q/ha) and BPT-5204 (48 q/ha).  </p>

<p>Cassava Mosaic Disease Resistant varieties PDPCMR1, Sree Raksha-1 and Sree Raksha-2 were assessed and found resistant to CMD  and with higher yields.  
</p>

<p>In Dolichos. assessed the performance of  photo-insensitive and pole type &#39;Arka Prasidhi&#39; of IIHR, Bengaluru and recorded 18.5 t/ha of pod yield compared to existing broad bean of 17.1 t/ha  and Farmer&#39;s practice of Desi Chikkudu with 16.3 t/ha.  </p>

<p>In Tomato &#39;Arka Abhed&#39; and &#39;Arka Samrat&#39; a multiple and triple disease resistant hybrids  were assessed and recorded higher yields of 48.2 t/ha and 51 t/ha and no incidence of diseases over private hybrid Sakata (31.0 t/ha).</p>

<p>Assessed the management module against Fall Armyworm in maize following seed treatment schedule, pheromone traps @ 10 traps/ha, spraying of Azadirachtin 10000 ppm 500 ml per ha and <em>Metarhizium anisopliae</em> @ 2.5 litres/ha  recorded an yield of 75.12 q/ha over farmer&#39;s practice (73.5 q/ha).  </p>
<p>Estrous Synchronization with Double PGF2 alpha Protocol resulted in 60% conception in buffaloes.</p>
<p>By feeding well balanced ration, the farmers could save Rs. 52 /- per day per animal.</p>
<p>Lucerne, a lugume fodder  introduced was well adopted in East Godavari climatic conditions with good net returns to farmers.</p>




<br>
<p>
	<strong>Front line demonstrations&nbsp;</strong> </p>

<p>
         <b> FLDs:</b>
</p>

<p>
Apart from cluster FLDs, a total of 19 FLDs were conducted during the year 2019-17. Highlights of FLDs are as follows.
</p>
<p>
Foliar spray of 1% PPFM at critical stage of crop growth in rice at 30 days interval resulted in 2.8 q/ha more yield compared to farmer&#39;s practice.</p>
<p>	Foliar spray of &#39;Pulse Wonder&#39; @ 5kg/ha in Blackgram resulted in reduced flower drop and increased yield of 1.5 q/ha.</p>
<p>	In Alternate Wet and Drying (AWD) (Field Tube Technology) demonstrated in rice resulted in reduced number of irrigations and also increase  in yield of 1. 8 q/ha.</p>
