<p>
	Krishi Vigyan Kendra (KVK) under the administrative control of Central Tobacco Research Institute was established (1983) at Kalavacharla village in Rajanagaram Mandal of East Godavari District, 25 km away from CTRI, Rajahmundry with disciplines Crop Production, Plant Protection, Horticulture, Animal Science Production and Home Science.   Recently (2018-19) KVK is supported by District Agro Meteorological Unit (DAMU) established with the financial aid by India Meteorological Department (IMD) with recruitment of an SMS (Agro-Meteorology) and a Agro-met Observer.   The KVK is actively engaged in transfer of viable and need based, skill oriented and socially sustainable technologies to cater to the needs of the rural and tribal people to improve their socio-economic status. This KVK is also conducting on-farm, off-farm, front-line demonstrations, vocational training programmes, farmers Awareness Programmes and advisory service to the farming community. The KVK has well developed instructional Farm with an area of 14.38 ha where different demonstration units are maintained. </p>

