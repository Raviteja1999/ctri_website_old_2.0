	<strong><span style="color:#b22222;">DIVISION OF CROP PROTECTION</span></strong></p>
	
<p>
	The division conducts research on the incidence and crop losses caused by insect pests and pathogens, etiology and epidemiology of diseases, bio-ecology of insect pests and developing integrated management schedules against insect pests and pathogens. The mandate of the division is to conduct basic and applied research on insect pests and disease management in tobacco and tobacco based cropping systems for development, demonstration and popularization of sustainable crop protection technologies. In order to manage the insect pest and diseases, scientists at the institute and its research stations have developed several promising crop protection technologies. The work done on neem and neem based products in controlling insect pests is note worthy. The entomologists have standardized mass production of bio-control agents particularly NPV. IPM modules with cultural, biological and need - based application of selective insecticides have been developed and successfully demonstrated in Farmers&rsquo; fields which helped in production of pesticide residue free tobaccos for export.</p>
